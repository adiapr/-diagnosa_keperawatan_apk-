    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Latihan Batuk Efektif</b>

    Observasi
    <ul>
        <li>Identifikasi kemampuan batuk</li>
        <li> Monitor adanya retensi sputum</li>
        <li> Monitor tanda dan gejala infeksi saluran napas</li>
        <li> Monitor input dan output cairan (mis. jumlah dan karakteristik)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Atur posisi semi-fowler atau fowler</li>
        <li>Pasang perlak dan bengkok di pangkuan pasien</li>
        <li>Buang sekret pada tempat sputum</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan tujuan dan prosedur batuk efektif</li>
        <li>Anjurkan tarik napas dalam melalui hidung selama 4 detik, ditahan selama 2 detik
            kemudian keluarkan dari mulut dengan bibir mencucu (dibulatkan) selama 8 detik</li>
        <li>Anjurkan mengulangi tarik napas dalam hingga 3 kali</li>
        <li>Anjurkan batuk dengan kuat langsung setelah tarik napas dalam yang ke-3</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborsi pemberian mukolitik atau ekspektoran, jika perlu</li>
    </ul>

    <br>
        <b>Manajamen Jalan Napas</b>

        Observasi
        <ul>
            <li>Monitor pola napas (frekuensi, kedalaman, usaha napas)</li>
            <li>Monitor bunyi napas tambahan (mis. gurgling, mengi, wheezing, ronkhi kering)  </li>
            <li>Monitor sputum (jumlah, warna, aroma)</li>
        </ul>

        Terapeutik
        <ul>
            <li>Pertahankan kepatenan jalan napas dengan head tilt dan chin lift (jaw thrust jika curiga
            trauma servikal)</li>
            <li> Posisikan semi-Fowler atau Fowler</li>
            <li> Berikan minum hangat</li>
            <li> Lakukan fisioterapi dada, jika perlu</li>
            <li> Lakukan penghisapan lendir kurang dari 15 detik</li>
            <li> Lakukan hiperoksigenisasi sebelum penghisapan endotrakeal</li>
            <li> Keluarkan sumbatan benda padat dengan forcep McGill</li>
            <li> Berikan oksigen, jika perlu</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan asupan cairan 2000 ml/hari, jika tidak kontraindikasi</li>
            <li>Ajarkan teknik batuk efektif</li>
        </ul>

        Kolaborasi
        <ul>
            <li>Kolaborasi pemberian bronkodilator, ekspektoran, mukolitik, jika perlu</li>
        </ul>

        <br>
        <b>Pemantauan Respirasi</b>

        Observasi
        <ul>
            <li>Monitor frekuensi, irama, kedalaman dan upaya napas</li>

            <li> Monitor pola napas (seperti bradipnea, takipnea, hiperventilasi, Kussmaul, Cheyne-
                Stokes, Biot, ataksik)</li>

            <li> Monitor kemampuan batuk efektif</li>
            <li> Monitor adanya produksi sputum</li>
            <li> Monitor adanya sumbatan jalan napas</li>
            <li> Palpasi kesimetrisan ekspansi paru</li>
            <li> Auskultasi bunyi napas</li>
            <li> Monitor saturasi oksigen</li>
            <li> Monitor nilai AGD</li>
            <li> Monitor hasil x-ray toraks</li>
        </ul>

        Terapeutik
        <ul>
            <li>Atur interval pemantauan respirasi sesuai kondisi pasien</li>
            <li>Dokumentasikan hasil pemantauan</li>
        </ul>

        Edukasi
        <ul>
            <li>Jelaskan tujuan dan prosedur pemantauan</li>
            <li>Informasikan hasil pemantauan, jika perlu</li>
        </ul>
