    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Dukungan Perawatan Diri: BAB/BAK</b>

    Observasi
    <ul>
        <li>Identifikasi kebiasaan BAK/BAB sesuai usia</li>
        <li>Monitor integritas kulit pasien  </li>
    </ul>

    Terapeutik
    <ul>
        <li>Buka pakaian yang diperlukan untuk memudahkan eliminasi</li>
        <li> Dukung penggunaan toilet/commodel/pispot/urinal secara konsisten</li>
        <li> Jaga privasi selama eliminasi</li>
        <li> Ganti pakaian pasien setelah eliminasi, jika perlu</li>
        <li> Bersihkan alat bantu BAK/BAK setelah digunakan</li>
        <li> Latih BAK/BAB sesuai jadwal, jika perlu</li>
        <li> Sediakan alat bantu (mis. kateter ekstemal, urinal), jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan BAK/BAB secara rutin</li>
        <li>Anjurkan ke kamar mandi/toilet, jika perlu</li>
    </ul>
    <b>Dukungan Perawatan Diri: Berpakaian</b>

        Observasi
        <ul>
            <li>Identifikasi usia dan budaya dalam membantu berpakalan/berhias</li>
        </ul>

        Terapeutik
        <ul>
            <li>Sediakan pakaian pada tempat yang mudah dijangkau</li>
            <li>Sediakan pakaian pribadi, sesuai kebutuhan</li>
            <li> Fasilitasi mengenakan pakaian, jika perlu</li>
            <li> Fasiltasi berhias (mis. menyisir rambut, merapikan kumis/jenggot)</li>
            <li> Jaga privasi selama berpakaian</li>
            <li> Tawarkan untuk laundry, jika perlu</li>
            <li> Berikan pujian terhadap kemampun berpakaian secara mandiri</li>
        </ul>

        Edukasi
        <ul>
            <li>Informasikan pakaian yang tersedia untuk dipilih, jika perlu</li>
            <li>Ajarkan mengenakan pakaian, jika perlu</li>
        </ul>

        <br>
        <b>Dukungan Perawatan Diri: Makan/Minum</b>

        Observasi
        <ul>
            <li>Identifikasi diet yang dianjurkan</li>
            <li>Monitor kemampuan menelan  </li>
            <li>Monitor status hidrasi pasien, jika perlu</li>
        </ul>

        Terapeutik
        <ul>
            <li>Ciptakan lingkungan yang menyenangkan selama makan</li>
            <li> Atur posisi yang nyaman untuk makan/minum</li>
            <li> Lakukan oral hygiene sebelum makan, jika perlu</li>
            <li> Letakkan makanan di sisi mata yeng sehat</li>
            <li> Sediakan sedotan untuk minum, sesuai kebutuhan</li>
            <li> Siapkan makanan dengan suhu yang meningkatkan nafsu makan</li>
            <li> Sediakan makanan dan minuman yang disukai</li>
            <li> Berikan bantuan saat makan/minum sesuai tingkat kemandirian, jika perlu</li>
            <li> Motivasi untuk makan di ruang makan, jika tersedia</li>
        </ul>

        Edukasi
        <ul>
            <li>Jelaskan posisi makanan pada pasien yang mangalami gangguan penglihatan dengan
                menggunakan arah jarum jam (mis. sayur di jam 12, rendang di jam 3)</li>
        </ul>
        Kolaborasi
        <ul>
            <li>Kolaborasi pemberian obat (mis analgesik, antiemetik), sesuai indikasi</li>
        </ul>

        <br>
        <b>Dukungan Perawatan Diri: Mandi</b>

        Observasi
        <ul>
            <li>Identifikasi usia dan budaya dalam membantu kebersihan diri</li>
            <li>Identifkasi jenis bantuan yang dibutuhkan  </li>
            <li>Monitor bersihan tubuh (mis. rambut, mulut, kulit, kuku)</li>
            <li>Monitor integritas kulit</li>
        </ul>

        Terapeutik
        <ul>
            <li>Sediakan peratatan mandi (mis. sabun, sikat gigi, shampoo, pelembap kulit)</li>
            <li> Sediakan lingkungan yang aman dan nyaman</li>
            <li> Fasilitasi menggosok gigi, sesuai kobutuhan</li>
            <li> Fasilitasi mandi, sesuai kebutuhan</li>
            <li> Pertahankan kebiasaan kebersihan diri</li>
            <li> Berikan bantuan sesuai tingkat kemandirian</li>
        </ul>

        Edukasi
        <ul>
            <li>Jelaskan manfaat mandi dan dampak tidak mandi terhadap kesehatan</li>
            <li>Ajarkan kepada keluarga memandikan pasien, jika perlu</li>
        </ul>
