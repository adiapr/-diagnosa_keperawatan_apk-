    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Manajemen Diare</b>

    Observasi
    <ul>
        <li>Identifikasi penyebab diare (mis. Inflamasi gastrointestinal, iritasi gastrointestinal, proses
            infeksi, malabsorbsi, ansietas, stress, efek obat-obatan)</li>
        <li> Identifikasi riwayat pemberian makanan</li>
        <li> Monitor warna, volume, frekuensi, dan konsistensi tinja</li>
        <li> Monitor tanda dan gejala hypovolemia (mis. takikardia, nadi terasa lemah, tekanan darah
        turun, turgor kulit turun, mukosa mulut kering, CRT melambat, BB menurun)</li>
        <li> Monitor iritasi dan userasi kulit didaerah parienal</li>
        <li> Monitor jumlah pengeluaran diare</li>
        <li> Monitor keamanan penyiapan kemanan</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan asupan cairan oral (mis. Larutan garam gula, oralit, pedialyte, renalyte)</li>
        <li> Pasang jalur intravena</li>
        <li> Berikan cairan intravena (mis. ringer asetat, ringer laktat, jika perlu)</li>
        <li> Ambil sempel darah untuk pemeriksaan daerah lengkap dan elektrolit</li>
        <li> Ambil sempel feses untuk kultur, jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan makanan porsi kecil dan sering secara bertahap</li>
        <li>Anjurkan menghindari makanan pembentuk gas, pedas dan mengandung laktosa</li> 
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian obat antimotilitas (mis. loperamide,divenoksilat)</li>
        <li>Kolaborasi pemberian obat antispasmodic/spasmolitik ( mis. papavenim, ekstakbeladona,
            mebeverine)</li>
        <li>Kolaborasi pemberian obat pengeras feses (mis. atapulgit, snektif, kaolin-fektin)</li> 
    </ul>

    <b>Pemantauan Cairan</b>

    Observasi
    <ul>
        <li>Monitor frekuensi dan kekuatan nadi</li>
        <li> Monitor frekuensi napas</li>
        <li> Monitor tekanan darah</li>
        <li> Monitor berat badan</li>
        <li> Monitor waktu pengisian kapiler</li>
        <li> Monitor elastisitas atau turgor kulit</li>
        <li> Monitor jumlah, warna dan berat jenis urine</li>
        <li> Monitor kadar albumin dan protein total</li>
        <li> Monitor hasil pemeriksaan serum (mis. osmolaritas serum, hematokrif, natrium, kalium,
        BUN)</li>
        <li> Monitor intake dan-output cairan</li>
        <li> Identifikasi tanda-tanda hipovolemia (mis. frekuensi nadi meningkat, nadi teraba lemah,
        tekanan darah menurun, tekanan nadi menyempit, turgor kulit menurun, membran mukosa
        kering, volume urin menurun, hematokrit meningkat, haus, lemah, konsentrasi urine
        meningkat, berat badan menurun dalam waktu singkat)</li>
        <li> Identifikasi tanda-tanda hipervolemia (mis. dispnea, edema perifer, edema anasarka, JVP
            meningket, CVP meningkat, refleks hepatojugular positif, berat badan menurun dalam waktu
        singkat)</li>
        <li> Identifikasi tanda-tanda hipervolemia (mis, dispnea, edema perifer, berat badan menurun
            dalam waktu singkat)</li>
        <li> Identifikasi faktor risiko ketidakseimbangan cairan (mis. prosedur pembedahan mayor,
            trauma/perdarahan, luka bakar, aferesis, obstruksi</li>
    </ul>

