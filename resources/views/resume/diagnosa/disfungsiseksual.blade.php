    Terapeutik
    <ul>
        <li>Sediakan materi dan media pendidikan kesehatan</li>
        <li> Jadwalkan pendidikan kesehatan sesuai kesepakatan</li>
        <li> Berikan kesempatan untuk bertanya</li>
        <li> Fasilitasi kesadaran keluarga terhadap anak dan remaja serta pengaruh media</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan anatomi dan fisiologi sistem reproduksi laki - laki dan perempuan</li>
        <li> Jelaskan perkembangan sesualitas sepanjang siklus kehidupan</li>
        <li> Jelaskan perkembangan emosi masa anak dan remaja</li>
        <li> Jelaskan pengaruh tekanan kelompok dan sosial terhadap aktivitas seksual</li>
        <li> Jelaskan konsekuensi negatif mengasuh anak pada usia dini (mis. kemiskinan, kehilangan karir dan pendidikan)</li>
        <li>Jelaskan risiko tertular penyakit menular seksual dan AIDS akibat seks bebas</li>
        <li> Anjurkan orang tua menjadi edukator seksualitas bagi anak - anaknya</li>
        <li> Anjurkan anak/remaja tidak melakukan aktivitas seksual di luar nikah</li>
        <li> Ajarkan keterampilan komunikasi asertif untuk menolak tekanan teman sebaya dan sosial
            dalam aktivitas seksual</li>
    </ul>

    <br>
