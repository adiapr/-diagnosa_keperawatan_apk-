    Terapeutik
    <ul>
        <li>Buka pakaian yang diperlukan untuk memudahkan eliminasi</li>
        <li> Dukung penggunaan toilet/commodel/pispot/urinal secara konsisten</li>
        <li> Jaga privasi selama eliminasi</li>
        <li> Ganti pakaian pasien setelah eliminasi, jika perlu</li>
        <li> Bersihkan alat bantu BAK/BAK setelah digunakan</li>
        <li> Latih BAK/BAB sesuai jadwal, jika perlu</li>
        <li> Sediakan alat bantu (mis. kateter ekstemal, urinal), jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan BAK/BAB secara rutin</li>
        <li>Anjurkan ke kamar mandi/toilet, jika perlu</li>
    </ul>

    <b>Pencegahan</b><br><br>

    Observasi
    <ul>
        <li>Identifikasi tanda dan gejala retensi atau inkontinensia urine</li>
        <li>Identifikasi faktor yang menyebabkan retensi atau inkontinensia urine  </li>
        <li>Monitor eliminasi urine (mis. frekuensi, konsistensi, aroma, volume, dan warna)</li>
    </ul>

