    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Latihan Memori</b>

    Observasi
    <ul>
        <li>Identifikasi masalah memori yang dialami</li>
        <li>Identifikasi kesalahan terhadap orientasi  </li>
        <li>Monitor perilaku dan perubahan memori selama terapi</li>
    </ul>

    Terapeutik
    <ul>
        <li>Rencanakan metode mengajar sesuai kemampuan pasien</li>
        <li> Stimulasi memori dengan mengulang pikiran yang terakhir kali diucapkan, jika perlu</li>
        <li> Koreksi kesalahan orientasi</li>
        <li> Fasilitasi mengingat kembali pengalaman masa lalu, jika perlu</li>
        <li> Fasilitasi tugas pembelajaran (mis. mengingat informasi verbal dan gambar)</li>
        <li> Fasilitasi kemampuan konsentrasi (mis. bermain kartu pasangan), jika perlu</li>
        <li> Stimulasi menggunakan memori pada peristiwa yang baru terjadi (mis. bertanya ke mana saja
            ia pergi akhir-akhir ini), jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan tujuan dan prosedur latihan</li>
        <li>Ajarkan teknik memori yang tepat (mis. imajinasi visual, perangkat mnemonik, pemainan
            memori, isyarat memori, teknik asosiasi, membuat daftar, komputer, papan nama)</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Rujuk pada terapi okupasi, jika perlu</li>
    </ul>

    <b>Orientasi Realita</b>

        Observasi
        <ul>
            <li>Monitor perubahan orientasi</li>
            <li>Monitor perubahan kognitif dan perilaku  </li>
        </ul>

        Terapeutik
        <ul>
            <li>Perkenalkan narna saat memulai interaksi</li>
            <li> Orientasikan orang, tempat, dan waktu</li>
            <li> Hadirkan realita (mis. beri penjelasan alternatif, hindari perdebatan)</li>
            <li> Sediakan lingkungan dan rutinitas secara konsisten</li>
            <li> Atur stimulus sonsorik dan lingkungan (mis. kunjungan, pemandangan, suara, pencahayaan,
            bau, dan sentuhan)</li>
            <li> Gunakan simbol dalam mengorientasikan lingkungan (mis. tanda, gambar, warna)</li>
            <li> Libatkan dalam terapi kelompok orientasi</li>
            <li> Berikan waktu istirahat dan tidur yang cukup, sesuai kebutuhan</li>
            <li> Fasilitasi akses informasi (mis. televisi, surat kabar, radio), jika perlu</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan perawatan diri secara mandiri</li>
            <li>Anjurkan penggunaan alat bantu (mis. kacamata, alat bantu dengar, gigi palsu)</li>
            <li>Ajarkan keluarga dalam perawatan orientasi realita</li>
        </ul>
