    <b>Pencegahan Aspirasi</b><br>

    <em>Observasi</em>
    <ul>
        <li>Monitor tingkat kesadaran, batuk, muntah, dan kemampuan menelan</li>
        <li> Monitor status pernafasan</li>
        <li> Monitor bunyi nafas terutama setelah makan/minum</li>
        <li> Periksa residu gaster sebelum memberi asupan oral</li>
        <li> Periksa kepatenan selang nasogastrik sebelum memberi asupan oral</li>
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Posisikan semi Fowler (30-45 derajat) 30 menit sebelum memberi asupan oral </li>
        <li> Pertahankan posisi semi Fowler (30 - 45 derajat) pada pasien tidak sadar </li>
        <li> Pertahankan kepatenan jalan napas (mis. teknik head tilt chin lift, jaw thrust, in line) </li>
        <li> Pertahankan pengembangan balon endotrachoal tube (ETT) </li>
        <li> Lakukan penghisapan jalan napas, jika produksi sekret meningkat </li>
        <li> Sediakan suction di ruangan </li>
        <li> Hindari memberi makan melalui selang gastrolntestinal, jika residu banyak </li>
        <li> Berikan makanan dengan ukuran kecil atau lunak </li>
        <li> Berikan obat oral dalam bentuk cair </li>
        <li>Anjurkan makan secara perlahan</li>
        <li>Ajarkan strategi mencegah aspirasi</li>
        <li>Ajarkan teknik mengunyah atau menelan, jika perlu</li>
