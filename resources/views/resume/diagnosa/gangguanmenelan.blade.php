    <em>Terapeutik</em>
    <ul>
        <li>Ciptakan lingkungan yang menyenangkan selama makan</li>
        <li> Atur posisi yang nyaman untuk mekan/minum</li>
        <li> Lakukan oral hygiene sebelum makan, jika perlu</li>
        <li> Letakkan makanan di sisi mata yang sehat</li>
        <li> Sediakan sodotan untuk minum, sesuai kebutuhan</li>
        <li> Siapkan makanan dengan suhu yang meningkatkan nafsu makan</li>
        <li> Sediakan makanan dan minuman yang disukai</li>
        <li> Berikan bantuan saat makan/minum sesuai tingkat kemandirian, jika perlu</li>
        <li> Motivasi untuk makan di ruang makan, Jika tersedia </li>
    </ul>

    <em>Edukasi</em>
    <ul>
        <li> Jelaskan posisi makanan pada pasien yang mengalami gangguan penglihatan dengan menggunakan
