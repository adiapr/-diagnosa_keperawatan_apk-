    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Pemantauan Respirasi</b>

    Observasi
    <ul>
        <li>Monitor frekuensi, irama, kedalaman dan upaya napas</li>
        <li> Monitor pola napas (seperti bradipnea, takipnea, hiperventilasi, Kussmaul, Cheyne-Stokes,
                Biot, ataksik)</li>
                <li> Monitor kemampuan batuk efektif</li>
                <li> Monitor adanya produksi sputum</li>
                <li> Monitor adanya sumbatan jalan napas</li>
                <li> Palpasi kesimetrisan ekspansi paru</li>
                <li> Auskultasi bunyi napas</li>
                <li> Monitor saturasi oksigen</li>
                <li> Monitor nilai AGD</li>
                <li> Monitor hasil x-ray toraks</li>
    </ul>

    Terapeutik
    <ul>
        <li>Atur interval pemantauan respirasi sesuai kondisi pasien</li>
        <li>Dokumentasikan hasil pemantauan</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan tujuan dan prosedur pemantauan</li>
        <li>Informasikan hasil pemantauan, jika perlu</li>
    </ul>

    br>
    <b>Terapi Oksigen</b>

    Observasi
    <ul>
        <li>Monitor kecepatan aliran oksigen</li>
        <li> Monitor posisi alat terapi oksigen</li>
        <li> Monitor aliran oksigen secara periodik dan pastikan fraksi yang diberikan cukup</li>
        <li> Monitor efektifitas terapi oksigen (min. oksimetri, analisa gas darah), jika perlu</li>
        <li> Monitor kemampuan melepaskan oksigen saat makan</li>
        <li> Monitor tanda-tanda hipoventilasi</li>
        <li> Monitor tanda dan gejala toksikasi oksigen den artelektasis</li>
        <li> Monitor tingkat kecemasan akibat terapi oksigen</li>
        <li> Monitor integritas mukosa hidung akibat pemasangan oksigen</li>
    </ul>

    Terapeutik
    <ul>
        <li>Bersihkan sekret pada mulut, hidung dan trakea, jika perlu</li>
        <li>Pertahankan kepatenan jalan napas</li>
        <li>Siapkan dan atur persiapan pemberian oksigen</li>
        <li>Berikan oksigen tambahan, jika perlu</li>
        <li>Tetap berikan oksigen saat pasien ditransportasi</li>
    </ul>

    Edukasi
    <ul>
        <li>Ajarkan pasien dan keluarga cara menggunakan oksigen di rumah</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi penentuan dosis oksigan</li>
        <li>Kolaborasi penggunaan oksigen saat aktvitas dan/atau tidur</li>
    </ul>
