    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Dukungan Tidur</b>

    Observasi
    <ul>
        <li>Identifikasi pola aktivitas dan tidur</li>
        <li>Identifikasi faktor pengganggu tidur (fisik dan/atau psikologis)  </li>
        <li>Identifikasi makanan dan minuman yang mengganggu tidur (mis. kopi, teh, alkohol, makan mendakati waktu tidur, minum banyak air sebelum tidur)</li>
        <li>Identifikasi obat tidur yang dikonsumsi</li>
    </ul>

    Terapeutik
    <ul>
        <li>Modifikasi lingkungan</li>
        <li> Batasi waktu tidur siang, jika perlu</li>
        <li> Fasilitasi menghilangkan stress sebelum tidur</li>
        <li> Tetapkan jadwal rutin</li>
        <li> Lakukan prosedur untuk meningkatkan kenyamanan (mis. pijat, pengaturan posisi, terapi)</li>
        <li> Sesuaikan jadwal pemberian obat dan/atau tindakan untuk menunjang siklus tidur-terjaga</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan pentingnya tidur cukup selama sakit</li>
        <li> Anjurkan menepati kebiasaan waktu tidur</li>
        <li> Anjurkan menghindari makanan/minuman yang mengganggu tidur</li>
        <li> Anjurkan penggunaan obat tidur yang tidak mengandung supresor terhadap tidur REM</li>
        <li> Ajarkan faktor-faktor yang berkontribusi terhadap gangguan pola tidur (mis. psikologis, gaya
            hidup, sering berubah shift bekerja)</li>
            <li> Ajarkan ralaksasi otot autogenik atau cara nonfarmakologi lainnya</li>
    </ul>

    <br>
    <b>Edukasi Aktivitas/Istirahat</b>

    Observasi
    <ul>
        <li>Identifikasi kesiapan dan kemampuan menerima informasi</li>
        <li>Monitor  </li>
    </ul>

    Terapeutik
    <ul>
        <li>Sediakan materi dan media pengaturan aktivitas dan istirahat</li>
        <li>Jadwalkan pemberian pendidikan kesehatan sesuai kesepakatan</li>
        <li>Berikan kesempatan kepada pasien untuk bertanya</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan pentingnya melakukan aktivitas fisik / olahraga secara rutin</li>
        <li> Anjurkan terlibat dalam aktivitas kelompok, aktivitas bermain atau aktivitas lainnya</li>
        <li> Anjurkan menyusun jadwal aktivitas dan istirahat</li>
        <li> Ajarkan cara mengidentifikasi kebutuhan istirahat (mis. kelelahan, sesak napas saat aktivitas)</li>
        <li> Ajarkan cara mengidentifikasi target jenis aktivitas sesual kemampuan</li>
    </ul>
