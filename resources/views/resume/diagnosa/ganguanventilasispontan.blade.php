    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Dukungan Ventilasi</b>

    Observasi
    <ul>
        <li>Identifikasi adanya kelelahan otot bantu napas</li>
        <li>Identifikasi efek perubahan posisi terhadap status pernapasan  </li>
        <li>Monitor status respirasi dan oksigenasi (mis. frekuensi dan kedalaman napas,
            penggunaan otot bantu napas, bunyi napas tambahan, saturasi oksigen)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Pertahankan kepatenan jalan napas</li>
        <li> Berikan posisi semi Fowler atau Fowler</li>
        <li> Fasilitasi mengubah posisi senyaman mungkin</li>
        <li> Berikan oksigenasi sesuai kebutuhan (mis. nasal kanul, masker wajah, masker rebreathing
            alau non rebreathing)</li>
        <li> Gunakan bag-valve mask, jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Ajarkan melakukan teknik relaksasi napas dalam</li>
        <li>Ajarkan mengubah posisi secara mandiri</li>
        <li>Ajarkan teknik batuk efektif</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian bronkhodilator, jika perlu</li>
    </ul>

    <br>
        <b>Pemantauan Respirasi</b>

        Observasi
        <ul>
            <li>Monitor frekuensi, irama, kedalaman dan upaya napas</li>

            <li> Monitor pola napas (seperti bradipnea, takipnea, hiperventilasi, Kussmaul, Cheyne-
            Stokes, Biot, ataksik)</li>

            <li> Monitor kemampuan batuk efektif</li>
            <li> Monitor adanya produksi sputum</li>
            <li> Monitor adanya sumbatan jalan napas</li>
            <li> Palpasi kesimetrisan ekspansi paru</li>
            <li> Auskultasi bunyi napas</li>
            <li> Monitor saturasi oksigen</li>
            <li> Monitor nilai AGD</li>
            <li> Monitor hasil x-ray toraks</li>
        </ul>

        Terapeutik
        <ul>
            <li>Atur interval pemantauan respirasi sesuai kondisi pasien</li>
            <li>Dokumentasikan hasil pemantauan</li>
        </ul>

        Edukasi
        <ul>
            <li>Jelaskan tujuan dan prosedur pemantauan</li>
            <li>Informasikan hasil pemantauan, jika perlu</li>
        </ul>
