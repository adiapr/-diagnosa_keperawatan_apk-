        <li> Monitor tingkat harga diri setiap waktu, sesuai kebutuhan</li>
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Monitor terlibat dalam verbalisasi positif untuk diri sendiri</li>
        <li> Motivasi menerima tantangan atau hal baru</li>
        <li> Diskusikan pernyataan tentang harga diri</li>
        <li> Diskusikan kepercayaan terhadap penilaian diri</li>
        <li> Diskusikan pengalaman yang meningkatkan harga diri</li>
        <li> Diskusikan persepsi negatif diri</li>
        <li> Diskusikan alasan mengkritik diri atau rasa bersalah</li>
        <li> Disukusikan penetapan tujuan realisis untuk mencapai harga diri yang lebih tinggi</li>
        <li> Diskusikan bersama keluarga untuk menetapkan harapan dan batasan yang jelas</li>
        <li> Berikan umpan balik positif atas peningkatan mencapai tujuan</li>
        <li> Fasilitasi lingkungan dan aktivitas yang meningkatkan harga diri </li>
    </ul>

