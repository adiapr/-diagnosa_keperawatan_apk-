    <ul>
        <li>Diskusikan tanggung jawab terhadap perilaku </li>
        <li> Jadwalkan kegiatan terstruktur </li>
        <li> Ciptakan dan pertahankan lingkungan dan kegiatan perawatan konsisten setiap dinas </li>
