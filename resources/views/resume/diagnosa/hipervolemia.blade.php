    <b>Manajemen Hipervolemia</b><br>

    <em>Observasi</em>
    <ul>
        <li>Periksa tanda dan gejala hopervolemia (mis. Orthopnea, dipsnea, edema, JVP/CVP meningkat,
            refleks hepatojugular positif, suara napas tambahan)</li>
            <li> Identifikasi penyebab hipervolemia</li>
            <li> Monitor status hemodinamik (mis. Frekuensi jantung, tekanan darah, MAP, CVP, PAP, PCWP,
            CO, CI), jika tersedia</li>
            <li> Monitor Intake den output cairan</li>
            <li> Monitor tanda hermokonsentrasi (mis, kadar natrium, BUN, hematokrit, berat jenis urine)</li>
            <li> Monitor tanda peningkatan tekanan onkotik plasma (mis. kadar protein dan albumin meningkat)
                <li> Monitor kecepatan infus secara ketat</li>
                <li> Monitor efek samping diuretik (mis. hipotensi ortortostatik, hipovolemla, hipokalemia
            hiponatremia)</li> 
    </ul>

