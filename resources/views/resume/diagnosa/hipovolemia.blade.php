    <b>Manajemen Syok Hipovolemik</b><br>

    <em>Observasi</em>
    <ul>
        <li> Monitor status kardiopulmonal (frekuensi dan kekuatan nadi, frekuensi napas, TD, MAP)</li> 
        <li> Monitor status oksigenasi(oksimetri nadi, AGD)</li> 
        <li> Monitor status cairan (masukan dan haluaran, turgor kulit, CRT)</li> 
        <li> Periksa tingkat kesadaran dan respon pupil</li> 
        <li> Periksa seluruh permukaan tubuh terhadap adanya DOTS (defomitiyldeformitas, open Wound/luka
            terbuka, tenderness/nyeri tekan, swolling/bengkak)</li> 
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Pertahankan jalan napas paten
        <li> Berikan oksigen untuk mempertahankan saturasi oksigen >94%</li> 
        <li> Persiapkan intubasi dan ventilasi mekanis, jika perlu</li> 
        <li> Lakukan penekanan langsung (direct pressure) pada perdarahan eksternal</li> 
        <li> Berikan posisi syok (modified Trendelenberg)</li> 
        <li> Pasang Jalur IV berukuran besar (mis. nomor 14 atau 16)</li> 
        <li> Pasang kateter uríne untuk menilai produksi urine</li> 
        <li> Pasang selang nasogastrik untuk dekompresi lambung</li> 
        <li> Ambil sampel darah untuk pemeriksaan darah lengkap dan elektrolit </li> 
    </ul>
 
