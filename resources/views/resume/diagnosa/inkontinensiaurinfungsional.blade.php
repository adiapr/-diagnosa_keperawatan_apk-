    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Latihan Berkemih</b>

    <em>Observasi</em>
    <ul>
        <li>Periksa kembali penyebab gangguan berkemih (mis. kognitif, kehilangan ekstremitas/fungsi
            ekstremitas, kehilangan penglihatan)</li>
        <li>Monitor pola dan kemampuan berkentih</li> 
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Hindari penggunaan kateter indwelling</li>
        <li>Siapkan area toilet yang aman</li>
        <li>Sediakan peralatan yang dibutuhkan dekat dan mudah dijangkau (mis, kursi komode, pispot)</li> 
    </ul>

    <em>Edukasi</em>
    <ul>
        <li>Jelaskan arah-arah menuju kamar mandi/toilet pada pasien dengan gangguan penglihatan</li>
        <li>Anjurkan intake cairan adekuat untuk mendukung output urine</li>
        <li>Anjurkan eliminasi normal dengan beraktivitas dan olahraga sesuai kemampuan</li>
    </ul> 

    <br>
    <b>Perawatan Inkontinensia Urine</b>

    <em>Observasi</em>
    <ul>
        <li>Identifikasi penyebab inkontinensia urine (mis. disfungsi neurologis, gangguan medula spinalis,
            gangguan refleks destrusor, obat-obatan, usia, riwayat operasi, gangguan fungsi kognitif)</li>
        <li>Identifikasi perasaan dan persepsi pasien terhadap inkontinensia urine yang dialaminya</li> 
        <li> Monitor keefektifan obat, pembedahan dan terapi modalitas berkemih</li> 
        <li> Monior kebiasaan BAK</li> 
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Bersihkan genital dan kulit sekitar secara rutin</li>
        <li> Berikan pujian atas keberhasilan mencegah inkontinensia</li>
        <li> Buat jadwal konsumsi obat-obat diuretik</li>
        <li> Ambil sampel urine untuk pemeriksaan urine lengkap atau kultur</li> 
    </ul>

    <em>Edukasi</em>
    <ul>
        <li>Jelaskan definisi, jenis Inkontinensia, penyebab inkontinensia urine</li> 
        <li> Jelaskan program penanganan inkontinensia urine</li> 
        <li> Jelaskan jenis pakaian dan lingkungan yang mendukung proses berkemih</li> 
        <li> Anjurkan membatasi konsumsi cairan 2-3 jam menjelang tidur</li> 
        <li> Ajarkan memantau cairan keluar dan masuk serta pola eliminasi urine</li> 
        <li> Anjurkan minum minimal 1500 cc/hari, jika tidak kontraindikasi</li> 
        <li> Anjurkan menghindari kopi, minuman bersoda, teh dan cokelat</li> 
        <li> Anjurkan konsumsi buah dan sayur untuk menghindari konstipasi</li> 
    </ul>

    <em>Kolaborasi</em>
    <ul>
        <li>Rujuk keahli inkontinensia, jika perlu</li> 
    </ul>


