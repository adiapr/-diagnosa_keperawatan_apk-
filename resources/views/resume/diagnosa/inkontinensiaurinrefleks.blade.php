    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Kateterisasi Urine</b>

    <em>Observasi</em>
    <ul>
        <li>Periksa kondisi pasien (mis. kesadaran, tanda-tanda vital, daerah parineal, distensi kantung
            kemih, inkontinensia urine, reflex berkemih)</li> 
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Siapkan peralatan, bahan-bahan dan ruangan tindakan</li>
        <li> Siapkan pasien bebaskan pakaian bawah dan posisikan dorsal rekomben (untuk wanita) dan
        supine (untuk laki-laki)</li>
        <li> Pasang sarung tangan</li>
        <li> Bershkan daerah perineal atau propesium dengan cairan NACL atau acuades</li>
        <li> Lakukan insersi kateter urine dengan menerapkan prinsip aseptic</li>
        <li> Sambungkan kateter urine dengan urine bag</li>
        <li> Isi balon dengan NACL 0,9% sesuai anjuran pabrik</li>
        <li> Viksasi selang kateter diatas simpisis atau dipaha</li>
        <li> Pastikan kantong urine ditempatkan lebih rendah dari kantung kemih</li>
        <li> Berikan label waktu pemasangan</li>
    </ul>

    <em>Edukasi</em>
    <ul>
        <li>Jelaskan tujuan prosedur pemasangan kateter urine</li>
        <li>Anjurkan menarik nafas saat insersi selang kateter</li> 
    </ul>

    <b>Perawatan inkontinensia urin</b>

    <em>Observasi</em>
    <ul>
        <li>Identifikasi penyebab inkontinensia urin (mis, disfungsi neurologis, gangguan medula
            spinalis, gangguang refleks destrusor, obat-obatan, usia, riwayat operasl, gangguan fungsi
        kognitif)</li>
        <li> Identifikasi perasaan dan persepsi pasien terhadap inkontinensia urin yang dialaminya</li>
        <li> Monitor keefektifan obat, operasi dan terapi modalitas berkemih</li>
        <li> Pantau kebiasaan BAK</li>
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Bersihkan alat kelamin dan kulit secara rutin</li>
        <li>Berikan pujian atas keberhasilan mencegah jadwal konsumsi obat-obat diuretik</li>
        <li>Ambil sampel urin untuk pemeriksaan urin lengkap atau kultur</li> 
    </ul>

    <em>Edukasi</em>
    <ul>
        <li>Jelaskan definisi, jenis inkontinensia, penyebab inkontinensia urin</li>
        <li> Jelaskan program penanganan dalam kontinensia urin</li>
        <li> Jelaskan jenis pakaian dan lingkungan yang mendukung proses berkernih</li>
        <li> Anjurkan membatasi konsurnsi cairan 2-3 jam sebelum tidur</li>
        <li> Ajarkan cairan keluar dan masuk serta pola eliminasi urin</li>
        <li> Anjurkan minimal 1500 cc/hari, jika tidak kontraindikasi</li>
        <li> Anjur kan menghindari kopi, minuman bersoda, teh dan cokelat</li>
        <li> Anjurkan konsumsi buah dan sayur untuk menghindari konstipasi</li>
    </ul>

    <em>Kolaborasi</em>
