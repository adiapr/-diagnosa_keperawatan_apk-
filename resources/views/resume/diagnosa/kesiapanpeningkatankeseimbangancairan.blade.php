    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Manajemen Cairan</b><br>

    <em>Observasi</em>
    <ul>
        <li>Monitor status hidrasi (mis. frekuensi nadi, kekuatan nadi, akral, pengisian kapiler, kelembaban
        mukosa, turgor kulit, tekanan darah)</li>
        <li> Monitor berat badan harian</li>
        <li> Monitor hasil pemeriksaan laboratorium (mis. hematokrit, Na, K, CI, berat jenis urine, BUN)</li>
        <li> Monitor status hemodinamik (mis. MAP, CVP, PAP, PCWP Jika tersedia)</li>
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Catat intake-output dan hitung balance cairan 24 jam</li>
        <li> Berikan asupan cairan, sesuai kebutuhan</li>
        <li> Berikan cairan intravena, jika perlu </li>
    </ul>

    <em>Kolaborasi</em>
    <ul>
        <li>Kolaborasi pemberian diuretik, jika perlu</li>
    </ul>

    <br>
    <b>Pemantauan Cairan</b><br>

    <em>Observasi</em>
    <ul>
        <li>Monitor frekuensi dan kekuatan nadi</li>
        <li> Monitor frekuensi napas</li>
        <li> Monitor tekanan darah</li>
        <li> Monitor berat badan</li>
        <li> Monitor waktu pengisian kapiler</li>
        <li> Monitor elasisitas atau turgor kulit</li>
        <li> Monitor jumlah, warna dan berat jenis urine</li>
        <li> Monitor kadar albumin dan protein total</li>
        <li> Monitor hasil pemeriksaan serum (mis. osmolaritas serum, hematokrit, natrium, kalium, BUN)</li>
        <li> Monitor intake dan-output cairan</li>
        <li> Identikasi tanda-tanda hipovolemia (mis. frekuensi nadi meningkat, nadi teraba lemah, tekanan
            darah menurun, tekanan nadi menyempit, turgor kulit menurun, membran mukosa kering, volume
            urin menurun, hematokrit meningkat, haus, lemah, konsentrasi urine meningkat, berat badan
        menurun dalam waktu singkat)</li>
        <li> Identifikasi tanda-tanda hipervolemia (mis. dispnea, edema perifer, edema anasarka, JVP
        meningkat, CVP meningkat, refleks hepatojugular positif, berat badan menurun dalam waktu singkat)</li>
        <li> Identifikasi faktor risiko ketidakseimbangan cairan(mis.prosedur pembedahan mayor,
            trauma/pendarahan, luka bakar, aferesis, obstruksi intestinal, peradangan pankreas, penyakit ginjal
            dan kelenjar, disfungsi intestinal)</li>
    </ul>

    <em>Terapeutik</em>
    <ul>
        <li>Atur interval waktu pemantauan sesuau dengan kondisi pasien </li>
        <li>Dokumentasikan hasil pemantauan </li>
    </ul>

    <em>Edukasi</em>
    <ul>
        <li>Jelaskan tujuan dan prosedur pemantauan </li> 
        <li> Informasikan hasil pemantauan, jika perlu </li>
    </ul>

