    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Manajemen Delirium</b>

    Observasi
    <ul>
        <li>Identifikasi faktor risiko dellirium (mis. usia >75 tahun, disfungsi kognitif, gangguan
            penglihatan/pendengaran, penurunan kemampuan fungsional, infeksi, hipo/hipertemia,
            hipoksia, malnutrisi, efak obat, toksin, gangguan tidur, stres)</li>
        <li>Identifikasi tipe delirium (mis. hipoaktif, hiperaktif, campuran)  </li>
        <li>Monitor status neurologis dan tingkat delirium</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan pencahayaan yang baik</li>
        <li> Sediakan jam dan kalender yang mudah terbaca</li>
        <li> Hindari stimulus sensorik berlebihan (mis. televisi, pengumuman interkom)</li>
        <li> Lakukan pengekangan fisik, sesuai indikasi</li>
        <li> Sediakan informasi tentang apa yang terjadi dan apa yang dapat terjadi selanjutnya</li>
        <li> Batasi pembuatan keputusan</li>
        <li> Hindari memvalidasi mispersepsi atau interpretasi realita yang tidak akurat (mis. halusinasi,
        waham)</li>
        <li> Nyatakan persepsi dengan cara yang tenang, meyakinkan, dan tidak argumentatif</li>
        <li> Fokus pada apa yang dikenali dan bermakna saat interaksi interpersonal</li>
        <li> Lakukan reorientasi</li>
        <li> Sediakan lingkungan fisik dan rutinitas harian yang konsisten</li>
        <li> Gunakan isyarat lingkungan untuk stimulasi memori, reorientasi, dan meningkatkan perilaku
            yang sesuai (mis. tanda, gambar, jam, kalendar, dan kode warna pada lingkungan)</li>
            <li> Berikan informasi baru secara perlahan, sedikit demi sedikit, diulang-ulang</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan kunjungan keluarga, jika perlu</li>
        <li>Anjurkan penggunaan alat bantu sensorik (mis. kacamata, alat bantu dengar, dan gigi palsu)</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian obat ansietas atau agitasi, jika perlu</li>
    </ul>

    <br>
        <b>Manajemen Halusinasi</b>

        Observasi
        <ul>
            <li>Monitor perilaku yang mengindikasi halusinasi</li>
            <li>Monitor dan sesuaikan tingkat aktivitas dan stimulasi lingkungan  </li>
            <li>Monitor isi halusinasi (mis. kekerasan atau membahayakan diri)</li>
        </ul>

        Terapeutik
        <ul>
            <li>Pertahankan lingkungan yang aman</li>
            <li>Lakukan tindakan keselamatan ketika tidak dapat mengontrol perilaku (mis. limit setting
                pembatasan wilayah, pengekangan fisik, seklusi)</li>
            <li>Diskusikan perasaan dan respons terhadap halusinasi</li>
            <li>Hindari perdebatan tentang validitas halusinasi</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan memonitor sendiri situasi terjadinya halusinasi</li>
            <li>Anjurkan bicara pada orang yang dipercaya untuk memberi dukungan dan umpan balik
                korektif terhadap halusinasi</li>
            <li>Anjurkan melakukan distraksi (mis. mendengarkan musik, melakukan aktivitas dan teknik
                relaksasi)</li>
            <li>Ajarkan pasien dan keluarga cara mengontrol halusinasi</li>
        </ul>

        Kolaborasi
        <ul>
            <li>Kolaborasi pemberian obat antipsikotik dan antiansietas, jika perlu</li>
        </ul>

        <br>
        <b>Manajemen Penyalahgunaan Zat</b>

        Observasi
        <ul>
            <li>Identifikasi penyebab ketergantungan atau penyalahgunaan zat</li>
            <li> Identifikasi perilaku denial tidak efektif</li>
            <li> Periksa tanda dan gejala intoksikasi</li>
            <li> Periksa pasien dan barang bawaannya secara acak</li>
        </ul>

        Terapeutik
        <ul>
            <li>Penuhi kebutuhan dasar seperti keamanan, kebersihan diri, kenyamanan, lingkungan tenang</li>
            <li> Perbaiki kesalahan konsepsi, tidak menyalahkan orang lain</li>
            <li> Pertahankan disiplin diri dengan pengawasan ketat</li>
            <li> Berikan batasan pada perilaku manipulatif</li>
            <li> Batasi akses penggunaan zat</li>
            <li> Hadapi secara konsisten, tidak menghakimi dan menghukum</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan berfokus pada saat ini dan masa depan, bukan masa lalu</li>
            <li> Anjurkan pasien dan keluarga mengikuti peraturan ketat rumah sakit secara efektif (mis.
            tidak menyelundupkan zat)</li>
            <li> Anjurkan mengikuti program kelompok</li>
            <li> Anjurkan untuk berobat jalan secara teratur dan mematuhi pengobatan seat pulang</li>
            <li> Ajarkan keterampilan pencegahan kekambuhan, keterampilan suportif dan tugas</li>
            perkembangan</li>
            <li> Jelaskan bahaya menggunakan alat invasif untuk memasukkan zat dalam tubuh (mis. abses,
                HIV)</li>
        </ul>

        Kolaborasi
        <ul>
            <li>Kolaborasi permberlan terapi subtitusi, sesual indikasi</li>
        </ul>
