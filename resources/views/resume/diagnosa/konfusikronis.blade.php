    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Manajemen Delirium</b>

    Observasi
    <ul>
        <li>Identifikasi faktor risiko dellirium (mis. usia >75 tahun, disfungsi kognitif, gangguan
            penglihatan/pendengaran, penurunan kemampuan fungsional, infeksi, hipo/hipertemia,
            hipoksia, malnutrisi, efak obat, toksin, gangguan tidur, stres)</li>
        <li>Identifikasi tipe delirium (mis. hipoaktif, hiperaktif, campuran)  </li>
        <li>Monitor status neurologis dan tingkat delirium</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan pencahayaan yang baik</li>
        <li> Sediakan jam dan kalender yang mudah terbaca</li>
        <li> Hindari stimulus sensorik berlebihan (mis. televisi, pengumuman interkom)</li>
        <li> Lakukan pengekangan fisik, sesuai indikasi</li>
        <li> Sediakan informasi tentang apa yang terjadi dan apa yang dapat terjadi selanjutnya</li>
        <li> Batasi pembuatan keputusan</li>
        <li> Hindari memvalidasi mispersepsi atau interpretasi realita yang tidak akurat </li>
        <li> Nyatakan persepsi dengan cara yang tenang, meyakinkan, dan tidak argumentatif</li>
        <li> Fokus pada apa yang dikenali dan bermakna saat interaksi interpersonal</li>
        <li> Lakukan reorientasi</li>
        <li> Sediakan lingkungan fisik dan rutinitas harian yang konsisten</li>
        <li> Gunakan isyarat lingkungan untuk stimulasi memori, reorientasi, dan meningkatkan perilaku
            yang sesuai (mis. tanda, gambar, jam, kalendar, dan kode warna pada lingkungan)</li>
            <li> Berikan informasi baru secara perlahan, sedikit demi sedikit, diulang-ulang</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan kunjungan keluarga, jika perlu</li>
        <li>Anjurkan penggunaan alat bantu sensorik (mis. kacamata, alat bantu dengar, dan gigi palsu)</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian obat ansiatas atau agitasi, jika perlu</li>
    </ul>

    <b>Manajemen Demensia</b>

        Observasi
        <ul>
            <li>Identifikasi riwayat fisik, sosial, psikologis, dan kebiasaan</li>
            <li>Identifikasi pola aktivitas (mis. tidur, minum obat, eliminasi, asupan oral, perawatan diri)  </li>
        </ul>

        Terapeutik
        <ul>
            <li>Sediakan lingkungan aman, nyaman, konsisten, dan rendah stimulus (mis. musik terang,
            dekorasi sederhana, pencahayaan memadai, makan bersarna pasien lain)</li>
            <li> Orientasikan waktu, tempat dan orang</li>
            <li> Gunakan distraksi untuk mengatasi masalah perilaku</li>
            <li> Libatkan keluarga dalam merencanakan, menyediakan, dan mengevaluasi perawatan</li>
            <li> Fasiltasi orientasi dengan simbol-simbol (mis, dekorasi, papan petunjuk, foto diberi nama,
            huruf besar)</li>
            <li> Libatkan kegiatan individu atau kelompok sesuai kemampuan kognitif dan minat</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan mmperbanyak istirahat</li>
            <li>Ajarkan keluarga cara perawatan demensia orang</li>
        </ul>

        <b>Terapi Validasi</b>

        Observasi
        <ul>
            <li>Identifikasi tahap gangguan kognitif (mis. malorientasi, bingung waktu, repetitif, atau vegetasi)</li>
            <li>Monitor dan refleksikan gestur  </li>
        </ul>

        Terapeutik
        <ul>
            <li>Hindari menggunakan strategi validasi jika bingung disebabkan oleh penyebab akut,
            reversibel, atau tahap vegetasi</li>
            <li> Identifikasi pasien kritis dari lokasi terlebih dahutu</li>
            <li> Identifikasi adanya luka/cedera</li>
            <li> Identifikasi keluhan utama pasien</li>
            <li> Identifikasi riwayat kesehatan pasien</li>
            <li> Lakukan survei primer, jika perlu</li>
            <li> Lakukan survei sistern sekunder semua sistem tubuh, jika perlu</li>
        </ul>

        Edukasi
        <ul>
            <li>Siapkan area dan peralatan untuk triase</li>
