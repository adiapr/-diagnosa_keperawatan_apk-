    <h3>2. Diagnosis keperawatan</h3>
    Gangguan komunikasi verbal b.d <b>gangguan pendengaran</b> d.d tidak mampu berbicara atau mendengar,
        menunjukkan respon tidak sesuai

    <h4>Luaran keperawatan</h4>
    Setelah dilakukan Intervensi Keperawatan diharapkan Komunikasi Verbal meningkat dengan kriteria hasil:
    <ul>
        <li>Kemampuan berbicara meningkat</li>
        <li>Kemampuan mendengar meningkat</li>
        <li>Kesesuaian ekspresi wajah/tubuh meningkat</li>
    </ul>

    <h4>intervensi keperawatan </h4>
    <b>Promosi Komunikasi: Defisit Bicara</b>
    <br><br>
    Observasi
    <ul>
        <li>Monitor kecepatan, tekanan, kuantitas, volume, dan diksi bicara</li>
        <li>Monitor proses kognitif, anatomis, dan fisiologis yang berkaitan dengan bicara (mis. memori, pendengaran, dan bahasa)</li>
        <li>Monitor frustrasi, marah, depresi, atau hal lain yang mengganggu bicara</li>
        <li>Identifikasi perilaku emosional dan fisik sebagai bentuk komunikasi</li>
    </ul>

    Terapeutik
    <ul>
        <li>Gunakan metode komunikasi alternatif (mis. menulis, mata berkedip, papan komunikasi dengan gambar dan huruf, isyarat tangan, dan komputer)</li>
        <li>Sesuaikan gaya komunikasi dengan kebutuhan (mis. berdiri di depan pasien, dengarkan dengan seksama, tunjukkan satu gagasan atau pemikiran
            sekaligus, bicaralah dengan perlahan sambil menghindari terÃ­akan, gunakan komunikasi tertulis, atau meminta bantuan keluarga untuk memahami
            ucapan pasien)</li>
        <li>Modifikasi lingkungan untuk meminimalkan bantuan</li>
        <li>Ulangi apa yang disampaikan pasien</li>
        <li>Berikan dukungan psikologis</li>
        <li>Gunakan juru bicara, jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan berbicara perlahan</li>
        <li>Ajarkan pasien dan keluarga proses kognitif, anatomis, dan fisiologis yang berhubungan dengan kemampuan berbicara</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Rujuk ke ahli patologi bicara atau terapis</li>
    </ul>
    <br>
    <b>Promosi Komunikasi: Defisit Pendengaran</b><br>
    <br>
    Observasi
    <ul>
        <li>-Periksa kemampuan pendengaran</li>
        <li>Identifikasi metode komunikasi yang disukai</li>
        <li>-Identifikasi metode komunikasi yang disukai pasien (mis. lisan, tulisan, gerakan bibir, bahasa isyarat)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Gunakan bahasa sederhana</li>
        <li>-Gunakan bahasa isyarat, jika perlu</li>
        <li>Gunakan bahasa sederhana  </li>
        <li>Verifikasi apa yang dikatakan atau ditulis pasien</li>
        <li>Fasilitasi penggunaan alat bantyu dengar</li>
        <li>Berhadapan dengan pasien secara langsung selama berkomunikasi</li>
        <li>Pertahankan kontak mata selama berkomunikasi</li>
        <li>Hindari merokok, mengunyah makana atau permen karet, dan menutup mulut saat berbicara</li>
        <li>Hindari kebisingan saat berkomunikasi</li>
        <li>Hindari berkomuniskasi lebih dari 1 meter dari pasien</li>
        <li>Lakukan irigasi telinga, jika perlu</li>
        <li>Pertahankan kebersihan telinga</li>
    </ul>

    Edukasi
    <ul>
        <li>-Anjurkan menyampaikan pesan dengan isyarat</li>
        <li>-Ajarkan cara membersihkan serumen dengan tepat</li>
    </ul>

