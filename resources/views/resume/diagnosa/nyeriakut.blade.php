    <h3>1. Diagnosis keperawatan </h3>
    <br>
    <h4 style="color:red"><b>Nyeri akut</b></h4> b.d <b>agen pencedera fisioloigs</b> d.d mengeluh nyeri, tapak meringis, bersikap protektif, gelisah,frekuensi nadi mengikat, sulit tidur
    <h4>Luaran Keperawatan</h4>
    Setelah dilakukan Intervensi Keperawatan diharapkan Tingkat nyeri menurun dengan kriteria hasil :
    <ul>
        <li>Keluhan nyeri menurun</li>
        <li>Meringis menurun</li>
        <li>Sikap protektif menurun</li>
        <li>Gelisah menurun</li>
        <li>Kesulitan tidur menurun</li>
        <li>Frekuensi nadi membaik</li>
    </ul>
    <h4>intervensi keperawatan :</h4>
    <b>Manajemennyeri</b><br><br>

    Observasi
    <ul>
        <li>Identifikasi skala nyeri</li>
        <li>Identifikasi lokasi , karakteristik , durasi , frekuensi , kualitas , intensitas nyeri</li>
        <li>Identifikasi respons nyeri non verbal</li>
        <li>Identifikasi faktor yang memperberat dan memperingan nyeri</li>
        <li>Identifikasi pengetahuan dan keyakinan tentang nyeri</li>
        <li>Identifikasi pengaruh nyeri pada kualitas hidup</li>
        <li>Identifikasi pengaruh budaya terhadap respon nyeri</li>
        <li>Monitor efek samping penggunaan analgetik</li>
        <li>Monitor keberhasilan terapi komplementer yang sudah diberikan</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan teknik nonfarmakologis untuk mengurangi rasa nyeri ( mis . TENS , hipnosis , akupresur , terapi musik , biofeedback , terapi
            pijat , aromaterapi , teknik imajinasi terbimbing kompres hangat / dingin , terapi bermain )</li>
        <li>Kontrol lingkungan yang memperberat rasa nyeri ( mis . suhu ruangan , pencahayaan , kebisingan )</li>
        <li>Fasilitasi istirahat dan tidur</li>
        <li>Pertimbangkan jenis dan sumber nyeri dalam pemilihan strategi meredakan nyeri</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan strategi meredakan nyeri</li>
        <li>Jelaskan penyebab , periode , dan pemicu nyeri</li>
        <li>Anjurkan memonitor nyeri secara mandiri</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Anjurkan menggunakan analgetik secara tepat</li>
        <li>Ajarkan teknik nonfarmakologis untuk mengurangi rasa nyeri</li>
        <li>Kolaborasi pemberian analgetik , jika perlu</li>
    </ul>
    <br>
    <br>
    <b>Pemberian analgesik</b><br><br>

    Observasi
    <ul>
        <li>Identifikasi karakteristik nyeri ( mis . pencetus , pereda , kualitas , lokasi , intensitas , frekuensi , durasi )</li>
        <li>Identifikasi riwayat alergi obat - Identifikasi kesesuaian jenis analgesik (mis.narkotika, non-narkotik, atau NSAID)
            dengan tingkat keparahan nyeri</li>
        <li>Monitor tanda - tanda vital sebelum dan sesudah pemberian analgesik</li>
        <li>Monitor efektifitas analgesik</li>
    </ul>

    Terapeutik
    <ul>
        <li>Diskusikan jenis analgesik yang disukai untuk mencapai analgesia optimal , jika perlu</li>
        <li>Pertimbangkan penggunaan infus kontinu , atau bolus opioid untuk mempertahankan kadar dalam serum</li>
        <li>Tetapkan target efektifitas analgesik untuk mengoptimalkan respons pasien</li>
        <li>Dokumentasikan respons terhadap efek analgesik dan efek yang tidak diinginkan</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan efek terapi dan efek samping obat</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian dosis dan jenis analgesik , sesuai indikasi</li>
    </ul>

