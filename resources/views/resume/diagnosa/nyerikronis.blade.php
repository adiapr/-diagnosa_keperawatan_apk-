    <h4>Luaran Keperawatan</h4>
    Setelah dilakukan Intervensi Keperawatan diharapkan Tingkat nyeri menurun dengan kriteria hasil :
    <ul>
        <li>Keluhan nyeri menurun</li>
        <li>Meringis menurun</li>
        <li>Sikap protektif menurun</li>
        <li>Gelisah menurun</li>
        <li>Kesulitan tidur menurun</li>
        <li>Frekuensi nadi membaik</li>
    </ul>
    <h4>Intervensi keperawatan :</h4>
    <b>Manajemennyeri</b><br><br>

    Observasi
    <ul>
        <li>Identifikasi skala nyeri</li>
        <li>Identifikasi lokasi , karakteristik , durasi , frekuensi , kualitas , intensitas nyeri</li>
        <li>Identifikasi respons nyeri non verbal</li>
        <li>Identifikasi faktor yang memperberat dan memperingan nyeri</li>
        <li>Identifikasi pengetahuan dan keyakinan tentang nyeri</li>
        <li>Identifikasi pengaruh nyeri pada kualitas hidup</li>
        <li>Identifikasi pengaruh budaya terhadap respon nyeri</li>
        <li>Monitor efek samping penggunaan analgetik</li>
        <li>Monitor keberhasilan terapi komplementer yang sudah diberikan</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan teknik nonfarmakologis untuk mengurangi rasa nyeri ( mis . TENS , hipnosis , akupresur , terapi musik , biofeedback , terapi
            pijat , aromaterapi , teknik imajinasi terbimbing kompres hangat / dingin , terapi bermain )</li>
        <li>Kontrol lingkungan yang memperberat rasa nyeri ( mis . suhu ruangan , pencahayaan , kebisingan )</li>
        <li>Fasilitasi istirahat dan tidur</li>
        <li>Pertimbangkan jenis dan sumber nyeri dalam pemilihan strategi meredakan nyeri</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan strategi meredakan nyeri</li>
        <li>Jelaskan penyebab , periode , dan pemicu nyeri</li>
        <li>Anjurkan memonitor nyeri secara mandiri</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Anjurkan menggunakan analgetik secara tepat</li>
        <li>Ajarkan teknik nonfarmakologis untuk mengurangi rasa nyeri</li>
        <li>Kolaborasi pemberian analgetik , jika perlu</li>
    </ul>
    <br>
    <br>
    <b>Perawatan kenyamanan </b><br><br>

    Observasi
    <ul>
        <li>Identifikasi gejala yang tidak menyenangkan (mis. mual, nyeri, gatal, sesak)</li>
        <li>Identifikasi pemahaman tentang kondisi, situasi dan perasaannya</li>
        <li>Identifikasi masalah emosional dan spiritual</li>
    </ul>

    Terapeutik
    <ul>
        <li>Berikan posisi yang nyaman</li>
        <li>Berikan kompres dingin atau hangat</li>
        <li>Ciptakan lingkungan yang nyaman</li>
        <li>Berikan pemijatan</li>
        <li>Dukung keluarga dan pengasuh terlibat dalam terapi/pengobatan/li>
        <li>Berikan terapi hipnosis/li>
        <li>Berikan terapi akupresur</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan mengenai kondisi dan pilihan terapi/pengobatan</li>
        <li>Ajarkan terapi relaksasi</li>
        <li>Ajarkan latihan pernapasan</li>
        <li>Ajarkan teknik distraksi dan imajinasi terbimbing</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian analgesik, antipruritus, antihistamin, jika perlu</li>
    </ul>
    <br>

    <b>Terapi relaksasi</b><br><br>
    Observasi
    <ul>
        <li>Identifikasi penurunan tingkat energi, ketidakmampuan berkonsentrasi, atau gejala lain yang
            mengganggu kemampuan kognitif</li>
        <li>Identifikasi teknik relaksasi yang pernah efektif digunakan</li>
        <li>Identifikasi kesediaan, kemampuan, dan penggunaan teknik sebelumnya</li>
        <li>Periksa ketegangan otot, frekuensi nadi, tekanan darah, dan suhu sebelum dan sesudah
            latihan</li>
        <li>Monitor respons terhadap terapi relaksasi</li>
    </ul>

    Terapeutik
    <ul>
        <li>Ciptakan lingkungan tenang dan tanpa gangguan dengan pencahayaan dan suhu ruang
            nyaman, jika memungkinkan</li>
        <li>Berikan informasi tertulis tentang persiapan dan prosedur teknik relaksasi</li>
        <li>Gunakan pakaian longgar</li>
        <li>Gunakan nada suara lembut dengan irama lambat dan berirama</li>
        <li>Gunakan relaksasi sebagai stralegi penunjang dengan analgetik atau tindakan medis lain,
            jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan tujuan, manfaat, batasan, dan jenis relaksasi yang tersedia (mis musik, medilasi,
            napas dalam, relaksasi otot progresif)</li>
        <li>Jelaskan secara rinci intervensi relaksasi yang dipilih</li>
        <li>Anjurkan mengambil posisi nyaman</li>
        <li>Anjurkan rileks dan merasakan sensasi relaksasi</li>
        <li>Anjurkan sering mengulangi atau melatih teknik yang dipilih</li>
        <li>Demonstrasikan dan latih teknik relaksasi (mis. napas dalam, peregangan, atau imajinasi
            terbimbing)</li>
    </ul>
