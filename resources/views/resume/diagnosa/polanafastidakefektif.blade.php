    <b>Manajemen Jalan Nafas</b>

    Observasi
    <ul>
        <li>Monitor pola napas (frekuensi, kedalaman, usaha napas)</li>
        <li>Monitor bunyi napas tambahan (mis. gurgling, mengi, wheezing, ronkhi kering)  </li>
        <li>Monltor sputum (jumlah, warna, aroma)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Pertahankan kepatenan jalan napas dengan head-tilt dan chin-lift (Jaw-thrust jika curiga
        trauma servikal)</li>
        <li> Posisikan seml-Fowler atau Fowler</li>
        <li> Berikan minum hangat</li>
        <li> Lakukan fisloterapi dada, jika perlu</li>
        <li> Lakukan penghisapan lendir kurang dari 15 detik</li>
        <li> Lakukan hiperoksigenasi sebelum penghisapan endotrakeal</li>
        <li> Keluarkan sumbatan benda padat dengan forsep McGill</li>
        <li> Berikan oksigen, jika pertu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan asupan cairan 2000 ml/hari, jika tidak kontraindikasi</li>
        <li>Ajarkan teknik batuk efektif</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian bronkodilator, ekspektoran, mukolitik, jika perlu</li>
    </ul>

    <br>
        <b>Pemantauan Respirasi</b>

        Observasi
        <ul>
            <li>Monitor frekuensi, irama, kedalaman dan upaya napas</li>
            <li>Monitor pola napas  </li>
            <li>Monitor kemampuan batuk efektif</li>
            <li> Monitor adanya produksi sputum</li>
            <li> Monitor adanya sumbatan jalan napas</li>
            <li> Palpasi kesimetrisan ekspansi paru</li>
            <li> Auskultasi bunyi napas</li>
            <li> Monitor saturasi oksigen</li>
            <li> Monitor nilai AGD</li>
            <li> Monitor hasil x-ray toraks</li>
        </ul>

        Terapeutik
        <ul>
            <li>Atur interval pemantauan respirasi sesuai kondisi pasien</li>
            <li>Dokumentasikan hasil pemantauan</li>
        </ul>

        Edukasi
        <ul>
            <li>Jelaskan tujuan dan prosedur pemantauan</li>
            <li>Informasikan hasil pemantauan, jika perlu</li>
        </ul>
