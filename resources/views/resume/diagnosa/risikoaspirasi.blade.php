    <h4>Intervensi keperawatan :</h4>
    <br>
    <b>Manajeman Jalan Napas</b>

    Observasi
    <ul>
        <li>Monitor pola napas (frekuensi, kedalaman, usaha napas)</li>
        <li>Monitor bunyi napas tambahan (mis. gurgling, mengi, wheezing, ronkhi kering)  </li>
        <li>Monitor sputum (jumlah, warna, aroma)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Pertahankan kepatenan jalan napas dengan head tilt dan chin lift (jaw thrust jika curiga
        trauma servikal)</li>
        <li> Posisikan semi-Fowler atau Fowler</li>
        <li>Berikan minum hangat</li>
        <li> Lakukan fisioterapi dada, jika perlu</li>
        <li> Lakukan penghisapan lendir kurang dari 15 detik</li>
        <li> Lakukan hiperoksigenisasi sebelum penghisapan endotrakeal</li>
        <li> Keluarkan sumbatan benda padat dengan forcep McGill</li>
        <li> Berikan oksigen, jika perlu</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan asupan cairan 2000 ml/hari, jika tidak kontraindikasi</li>
        <li>Ajarkan teknik batuk efektif</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi pemberian bronkodilator, ekspektoran, mukolitik, jika perlu</li>
    </ul>

    <br>
        <b>Pencegahan Aspirasi</b>

        Observasi
        <ul>
            <li>Monitor tingkat kesadaran, batuk, muntah, dan kemampuan menelan</li>
            <li> Monitor status pernapasan</li>
            <li> Monitor bunyi napas, terutama makan/minum</li>
            <li> Periksa residu gaster sebelum memberi asupan oral</li>
            <li> Periksa kepatenan selang nasogastric sebelum memberikan asupan oral</li>
        </ul>

        Terapeutik
        <ul>
            <li>Posisikan semi Fowler (30-45 derajat) 30 menit sebelum memberikan asupan oral</li>
            <li> Pertahankan posisi semi Fowler (30-45 derajat) pada pasien tidak sadar</li>
            <li> Pertahankan kepatenan jalan napas (mis. teknik head tilt, chin lift, jaw thrust, in line)</li>
            <li> Pertahankan pengembangan balon endotracheal tube (ETT)</li>
            <li> Lakukan penghisapan jalan napas, jika produksi sekret meningkat</li>
            <li> Sediakan suction di ruangan</li>
            <li> Hindari memberi makan melalui selang gastrointestinal, jika residu banyak</li>
            <li> Berikan makanan dengan ukuran kecil atau lunak</li>
            <li> Berikan obat oral dalam bentuk cair</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan makan secara perlahan</li>
            <li>Ajarkan strategi mencegah aspirasi</li>
            <li>Ajarkan teknik mengunyah atau menelan, jika perlu</li>
        </ul>

