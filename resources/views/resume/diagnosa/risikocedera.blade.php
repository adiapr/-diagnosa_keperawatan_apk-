        <li> Pastikan roda tempat tidur atau kursi roda dalam kondisi terkunci</li>
        <li> Gunakan pengaman tempat tidur sesuai dengan kebijakan fasilitas pelayanan kesehatan</li>
        <li> Pertimbangkan penggunaan alam elektronik pribadi atau alarm sensor pada tempat tidur atau kursi</li>
        <li> Diskusikan mengenai latihan dan terapi fisik yang diperlukan</li>
        <li> Diskusikan mengenai alat bantu mobilitas yang sesuai (mis. tongkat atau alat bantu Jalan)</li>
