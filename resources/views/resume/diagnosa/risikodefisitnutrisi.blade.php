        <li> Berikan penguatan positif terhadap keberhasilan target dan perubahan prilaku</li>
        <li> Berikan konsekuensi jika tidak mencapai target sesuai kontrak</li>
        <li> Rencanakan program pengobatan untuk perawatan di rumah (mis.medis, konseling)</li>
    </ul>

    Edukasi
    <ul>
        <li>Anjurkan membuat catatan harian tentang perasaan dan situasi pemicu pengeluaran makanan
            (mis. pengeluaran yang disengaja, muntah, aktivitas berlebihan)</li>
        <li>Ajarkan pengaturan diet yang tepat</li>
        <li>Ajarkan keterampilan koping untuk penyelesaian masalah perilaku makan</li>
    </ul>

    Kolaborasi
    <ul>
        <li>Kolaborasi dengan ahli gizi tentang target berat badan, kebutuhan kalori dan pilihan makanan</li>
    </ul>

