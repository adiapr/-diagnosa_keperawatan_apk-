    <h4>Intervensi keperawatan :</h4>
    <b>Pemantauan Elektrolit</b><br><br>

    Observasi
    <ul>
        <li>Identifikasi kemungkinan penyebab ketidakseimbangan elektrolit</li>
        <li> Monitor kadar elektrolit serum</li>
        <li> Monitor mual, muntah dan diare</li>
        <li> Monitor kehilangan cairan, jika perlu</li>
        <li> Monitor tanda dan gejala hipokalemia (mis. kelemahan otot, interval QT memanjang
            gelombang T datar atau terbalik, depresi segmen ST, gelombang U, kelelahan, parestesia
            penurunan refleks, anoreksia, konstipasi, motilitas usus menurun, pusing, depresi
            pernapasan)</li>
            <li> Monitor tanda dan gejala hiperkalemia (mis. peka rangsang, gelisah, mual, muntah,
            takikardia mengarah ke bradikardia, fibrilasi/takikardia ventrikel, gelombang T tinggi,
            gelombang P datar, kompleks QRS tumpul, blok jantung mengarah asistol)</li>
            <li> Monitor tanda dan gejala hiponatremia (mis. disorientasi, otot berkedut, sakit kepala,
            membrane mukosa kering, hipotensi postural, kejang, letargi, penurunan kesadaran)</li>
            <li> Monitor tanda dan gejala hipernatremia (mis. haus, demam, mual, muntah, gelisah, peka
            rangsang, membran mukosa kering, takikardia, hipotensi, letargi, konfusi, kejang)</li>
            <li> Monitor tanda dan gejala hipokalsemia (mis. peka rangsang, tanda Chvostek [spasme otot
            wajah], tanda Trousseau [spasme karpal], kram otot, interval QT memanjang)</li>
            <li> Monitor tanda dan gejala hiperkalsemia (mis. nyeri tulang, haus, anoreksia, letargi,
            kelemahan otot, segmen QT memendek, gelombang T lebar, komplek QRS lebar,
            interval PR memanjang)</li>
            <li> Monitor tanda dan gejala hipomagnesemia (mis. depresi pernapasan, apatis, tanda
            Chvostek, tanda Trousseau, konfusi, disritmia)</li>
            <li> Monitor tanda dan gejala hipermagnesemia (mis. kelemahan otot, hiporefleks,
            bradikardia, depresi SSP, letargi, koma, depresi)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Atur interval waktu pemantauan sesuai dengan kondisi pasien</li>
        <li>Dokumentasikan hasil pemantauan</li>
    </ul>

    Edukasi
    <ul>
        <li>Jelaskan tujuan prosedur pemantauan</li>
        <li>Informasikan hasil pemantauan, jika perlu</li>
    </ul>
