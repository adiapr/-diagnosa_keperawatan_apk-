            <li> Monitor saturasi oksigen</li>
            <li> Identifikasi stratifikasi pada sindrom koroner akut (mis. skor TIMI, Kilip, Crusade)</li>
        </ul>

        Terapeutik
        <ul>
            <li>Pertahankan tirah baring minimal 12 jam</li>
            <li> Pasang akses intavena</li>
            <li> Puasakan hingga bebas nyeri</li>
            <li> Berikan terapi relaksasi untuk mengurangi ansietas dan stres</li>
            <li> Sediakan lingkungan yang kondusif untuk berstirahat dan pemulihun</li>
            <li> Siapkan menalani intervensi koroner perkutan, jika perlu</li>
            <li> Berikan dukungan emosional dan spiritual</li>
        </ul>

        Edukasi
        <ul>
            <li>Anjurkan segera melaporkan nyeri dada</li>
            <li> Anjurkan menghindari manuver Valsava (mis. mengeden saat BAB atau batuk)</li>
            <li> Jelaskan tindakan yang dijalani pasien</li>
            <li> Ajarkan teknik menurunkan kecemasan dan wetaktan</li>
        </ul>

        Kolaborasi
        <ul>
            <li>Kolaborasi pemberian antiplatelet, jika perlu</li>
            <li> Kolaborasi penberian antiangina (mis nitrogliserin, beta blocker, calcium channel blocker)</li>
            <li> Kolaborasi pemberian morfin, jika perlu</li>
            <li> Kolaborasi pemberian inotropik, jika perlu</li>
            <li> Kolaborasi permberian obat untuk mencegah manuver Valsava (mis. pelunak tinja,
            antermetik)</li>
            <li> Kolaborasi pencegahan trombus dengan antikoagulan, jika perlu</li>
            <li> Kolaborasi pemeriksaan x-ray dada, jika perlu</li>
        </ul>
