    <b>Pencegahan</b>

    Observasi
    <ul>
        <li>Identifikasi tanda/gejala primer penurunan curah jantung (meliputi dispnea, kelelahan,
        edema, ortopnes, paroxyamal nocturnal dyspnea, peningkatan CVP)</li>
        <li> Identifikasi tanda/gejala sekunder penurunan curah jantung (meliputi peningkatan berat
        badan, hepatomegali, distensi vena jugularis, palpitasi, ronkhi basah, oliguria, betuk, pucat)</li>
        <li> Monitor tekanan darah (termasuk tekanan darah ortostatik, jika perlu)</li>
        <li> Monitor intake dan output cairan</li>
        <li> Monitor berat badan setiap hari pada waktu yang sama</li>
        <li> Monitor saturasi oksigen</li>
        <li> Monitor keluhan nyeri dada (mis. intensitas, lokasi, radiasi, durasi, presivitasi yang
        mengurang nyeri)</li>
        <li> Monitor EKG 12 sadapan</li>
        <li> Monitor aritmia (kelainan irama dan frekuensi)</li>
        <li> Monitor nilai laboratorium jantung (mis. elektrolit, enzim jantung, BNP, NTpro-BNP)</li>
        <li> Monitor fungsi alat pacu jantung</li>
        <li> Periksa tekanan darah dan frekuensi nadi sebelum dan sesudah aktivitas</li>
        <li> Periksa tekanan darah dan frekuensi nadi sebelum pemberian obat (mie. beta blocker, ACE
            inhibitor, calolum channel blocker, digoksin)</li>
    </ul>

    Terapeutik
    <ul>
        <li>Posisikan pasien semi-Fowler atau Fowler dengan kaki ke bawah atau posisi nyaman
        <li> Berikan diet jantung yang sesuai (mis, batasi asupan kafein, natrium, kolesteral, dan
        makanan tinggi lernak)</li>
        <li>Gunakan stocking elastis atau pneumatik intermiten, sesuai indikasi</li>
        <li> Fasilitasi pasien dan keluarga untuk modifikasi gaya hidup sehat</li>
        <li> Berikan terapi relaksasi untuk mengurangi stres, jika perlu</li>
        <li> Berikan dukungan emosional dan spiritual</li>
        <li> Berikan oksigen untuk mampertahankan saturasi oksigen >34%</li>
    </ul>

