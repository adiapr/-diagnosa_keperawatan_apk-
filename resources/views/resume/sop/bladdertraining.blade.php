    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   BLADDER TRAINING</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Bladder training adalah salah satu upaya untuk mengembalikan fungsi kandung kemih yang mengalami gangguan ke keadaan normal atau ke fungsi optimal neurogenik (potter & perry, 2010)</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Terapi ini bertujuan memperpanjang interval berkemih yang normal dengan berbagai teknik distraksi atau teknik relaksasi sehingga frekuensi berkemih dapat berkurang, hanya 6-7 kali per hari atau 3-4 jam sekali. Melalui latihan, penderita diharapkan dapat menahan sensasi berkemih. Latihan ini dilakukan pada pasien pasca bedah yang di pasang kateter (Suharyanto,2008). </td>
        </tr>
        <tr>
            <td>Kebijakan</td>
            <td>
                <ol>
                    <li>Menggunakan kateter yang lama </li>
                    <li>pasien yang mengalami inkontinensia urin </li>
                    <li>klien yang akan di lakukan pelepasan dower kateter </li>
                    <li>pada klien post operasi. </li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Peralatan</td>
            <td>
                <ol>
                    <li>jam </li>
                    <li>air minum dalam tempatnya </li>
                    <li>obat diuretik jika diperlukan </li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur pelaksanaan</td>
            <td>
                <ol>
                    <li>Persiapan pasien
                        <ul>
                            <li>jelaskan maksud dan tujuan dari tindakan tersebut </li>    
                            <li>jelaskan prosedur tindakan yang harus dilakukan klien </li>    
                        </ul>    
                    </li>
                    <li>Beritahu klien minum yang banyak sekitar 30 menit sebelum waktu jadwal untuk berkemih. 
                        <ul>
                            <li>beritahu klien untuk menahan berkemih dan memberitahu perawat jika rangsangan berkemihnya tidak dapat di tahan. </li>
                            <li>klien di suruh menunggu atau menahan berkemih dalam rentang waktu yang telah ditentukan 2-3 jam sekali </li>
                            <li>30 menit kemudian, tepat pada jadwal berkemih yang telah ditentukan, mintalah klien untuk memulai berkemih dengan teknik latihan dasar panggul.</li>
                        </ul>
                    </li>
                    <li>Latihan 
                        <ol type="A">
                            <li>Latihan I 
                                <ol type="a">
                                    <li>Instruksikan klien untuk berkonsentrasi pada otot panggul </li>
                                    <li>Minta klien berupaya menghentikan aliran urine selama berkemih kemudian memulainya kembali.</li>
                                    <li>Praktikan setiap kali berkemih</li>
                                </ol>
                            </li>
                            <li>Latihan II 
                                <ol type="a">
                                    <li>Minta kllien untuk mengembil posisi duduk atau berdiri </li>
                                    <li>Instruksikan klien untuk mengencangkan otot-otot di sekitar anus </li>
                                </ol>
                            </li>
                            <li>Latihan III 
                                <ol type="a">
                                    <li>Minta klien mengencangkan otot bagian posterior dan kemudian kontraksikan otot anterior secara perlahan sampai hitungan ke empat </li>
                                    <li>Kemudian minta klien untuk merelaksasikan otot secara keseluruhan </li>
                                    <li>Ulangi latihan 4 jam sekali, saat bangun tidur sealam 3 bulan </li>
                                </ol>
                            </li>
                            <li>Latihan IV 
                                <ol type="a">
                                    <li>Apabila memungkinkan, anjurkan Sit-Up yang dimodifikasi (lutut di 
                                        tekuk) kepada klien 
                                        </li>
                                </ol>
                            </li>
                        </ol>
                    </li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Evaluasi</td>
            <td>
                <ol type="a">
                    <li>Klien dapat menahan berkemih dalam 6-7 kali perhari atau 3-4 jam sekali </li>
                    <li>Bila tindakan tersebut dirasakan belum optimal atau terdapat gangguan : 
                        <ol>
                            <li>Maka metode diatas dapat di tunjang dengan metode rangsangan dari eksternal misalnya dengan suara aliran air dan menepuk paha bagian dalam </li>
                            <li>Menggunakan metode untuk relaksasi guna membantu pengosongan kandung kemih secara total, misalnya dengan membaca dan menarik napas dalam.</li>
                            <li>Menghindari minuman yang mengandung kafein. </li>
                            <li>Minum obat diuretik yang telah diprogramkan atau cairan untuk 
                                meningkatkan diuretik.
                                </li>
                        </ol>
                    </li>
                </ol>
            </td>
        </tr> 
    </table>
