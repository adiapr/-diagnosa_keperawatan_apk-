    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   EDUKASI DIET PASIEN</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Memberikan pendidikan atau penyuluhan kepada pasien dan/atau
                keluarga berhubungan dengan diet yang dianjurkan kepada pasien</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Sebagai acuan penerapan langkah-langkah untuk menyiapkan pasien

                agar mengikuti diet yang dianjurkan dengan benar.</td>
        </tr>
        <tr>
            <td>Langkah - Langkah</td>
            <td>
                <ol>
                    <li>Kaji pengetahuan pasien saat ini tentang diet yang dianjurkan</li>
                    <li> Tentukan persepsi pasien tentang diet dan harapan tentang
                    tingkat pemenuhan diet.</li>
                    <li> Berikan penjelasan tentang diet yang ditentukan</li>
                    <li> Jelaskan tujuan diet</li>
                    <li> Berikan penjelasan tentang berapa lama diet harus dilakukan</li>
                    <li> Berikan penjelasan kepada pasien tentang bagaimana membuat
                    agenda makan secara tepat</li>
                    <li> Instruksikan pasien untuk mengikuti diet yang dianjurkan dan
                    menghindari makanan yang merupakan pantangan.</li>
                    <li> Berikan penjelasan tentang interaksi obat dan makanan yang
                    mungkin terjadi</li>
                    <li> Bantu pasien untuk mengakomodasi pilihan makanan dalam diet
                    yang ditentukan</li>
                    <li> Bantu pasien dalam melakukan penggantian bahan makanan
                        untuk mendapatkan resep faforit sesuai dengan diet yang
                    dianjurkan.</li>
                    <li> Berikan penjelasan kepada pasien cara membaca label makanan
                    dan memilih makanan dengan tepat.</li>
                    <li> Observasi kemampuan pasien memilih makanan sesuai dengan
                    diet. yang telah ditentukan.</li>
                    <li> Beriakn secara tertulis waktu makan pasien.</li>
                    <li> Berikan penguatan terhad informasi yang diberikan oleh tim
                    kesehatan lain</li>
                    <li> Libatkan keluarga</li>
                    <li> Lakukan evaluasi dan verifikasi hasil pemberian edukasi</li>
                    <li> Catat hasil pemberian edukasi pasien rawat inap dalam lembar
                        informasidan edukasi RM, pada pasien rawat
                        jalan,dokumentasikan pemberian edukasi tentang diet dalam
                        lembar informasi dan edukasi rawat jalan.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Hal yang perlu diperhatikan</td>
            <td>
                Berikan diet sesuai dengan kebutuhan pasien dengan memperhatikan
            diagnosa penyakit pasien
            </td>
        </tr>
