        <tr>
            <td>Tahap Kerja</td>
            <td>
                <ol>
                    <li>Meminta pasien untuk menutup mata dengan lembut dan perlahan.</li>
                    <li>Meminta pasien untuk menarik nafas dalam, kemudian hembuskan dengan panjang (minimal 3 kali).</li>
                    <li>Meminta pasien untuk mengamati tubuhnya, menemukan ketegangan pada otot tertentu kemudian kendurkan otot tersebut.</li>
                    <li>Terapis menghitung dari hitungan ketiga sampai 1. Pada hitungan kedua ajak pasien untuk bersiap-siap menuju “tempat 
                        pengasingan” atau “tempat kedamaian” yang menenangkan dan 
                        menyenangkan, dan pada hitungan ke satu ajak pasien menuju tempat tersebut.
                        </li>
                    <li>Meminta pasien untuk merasakan rumput atau pasir yang diinjak, angin semilir yang sejuk di tempat tersebut.</li>
                    <li>Meminta pasien untuk melihat latar depan , hamparan pegunungan, awan tipis yang menyelimuti, mentari yang menyinari danau atau laut, burung yang berterbangan, pepohonan yang hijau, bunga-bunga yang mekar, lebah 
                        yang menghisap sari bunga.
                        </li>
                    <li>Meminta pasien untuk mendengarkan bunyi pepohonan diterpa angin, burung yang berkicau, lebah madu yang mendengung, dan air yang bergemericik.</li>
                    <li>Gunakan distorsi waktu, seakan-akan pasien telah berada disana selama satu jam atau lebih.</li>
                    <li>Minta pada pasien untuk 
                        menggunakan penguatan dan mengulangi seperti : “saya dapat rilex kapan saja, saya hidup rukun dan damai, kedamaian ada dalam diri saya.”
                        </li>
                    <li>Terapis menghitung dari tiga ke satu. Pada hitungan ke satu 
                        minta pasien untuk merasakan relax dan minta untuk membuka mata secara perlahan-lahan dan tetap relax.
                        </li>
                </ol>
            </td>
        </tr> 
