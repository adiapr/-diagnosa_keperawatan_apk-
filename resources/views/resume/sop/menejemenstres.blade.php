        <tr>
            <td>Tujuan</td>
            <td>
                <ol>
                    <li>Mengatur Diri</li>
                    <li>Berpikir Rasional</li>
                    <li>Menenangkan Diri</li>
                    <li>Membantu Mencari Jalan Keluar</li>
                    <li>Meningkatkan Produktivitas</li>
                    <li>Pematangan Diri</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Pasien</td>
            <td>
                <ol>
                    <li>Menyiapkan kondisi lingkungan yang nyaman untuk
                        melakukan kegiatan relaksasi visualisasi.
                        </li>
                    <li>Menjelaskan prinsip dan prosedur yang akan dilakukan dengan cermat agar dimengerti pasien (gunakan otak kanan yang bersifat menerima).</li>
                    <li>Menjelaskan lama waktu visualisasi yang efektif (10 - 30 menit).</li>
                    <li>Meminta kepada pasien untuk memposisikan badan senyaman mungkin, tidak saling bersentuhan dengan anggota badan lain serta benda disekitar serta fokus, dan tenang.</li>
                </ol>
            </td>
        </tr>
