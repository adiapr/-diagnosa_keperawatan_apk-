    <h5 align="center">
        <b> STANDAR OPERASIONAL PROSEDUR  <br> PEMBERIAN KOMPRES HANGAT</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Kompres hangat adalah memberikan rasa hangat pada daerah
                tertentu dengan menggunakan cairan atau alat yang
                menimbulkan rasa hangat pada bagian tubuh yang memerlukan.</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>
                <ol>
                    <li>Memperlancar sirkulasi darah</li>
                    <li>Menurunkan suhu tubuh</li>
                    <li>Mengurangi rasa sakit</li>
                    <li>Memberi rasa hangat, nyaman, dan rasa tenang pada klien</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Indikasi</td>
            <td>
                <ol>
                    <li>Klien hipertermi (suhu tubuh yang tinggi)</li>
                    <li>Klien dengan perut kembung</li>
                    <li>Spasme/kram otot atau nyeri</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Alat dan Bahan</td>
            <td>
                <ol>
                    <li>Air panas</li>
                    <li>Washlap/buli-buli panas</li>
                    <li>Pengalas/perlak</li>
                    <li>Handuk kering</li>
                    <li>Kom</li>
                    <li>Sarung tangan</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Pasien</td>
            <td>
                <ol>
                    <li>Mengkaji keadaan umum pasien dan tanda-tanda vital atau tingkat nyeri pada pasien</li>
                    <li>Menjelaskan tentang prosedur yang akan dilakukan dan kontrak waktu</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur</td>
            <td>
                <ol>
                    <li>Salam therapeutic</li>
                    <li>Identifikasi kembali pasien dan periksa tanda-tanda vital</li>
                    <li>Memberitahu pasien bahwa tindakan akan segera dimulai</li>
                    <li>Menyiapkan alat-alat sesuai kebutuhan (kompres hangat basah
                        atau kompres hangat kering).
                        <br>
                        Jika kompres hangat kering :
                        <ol type="a">
                            <li>Menyiapkan buli-buli</li>
                            <li>Membuka tutupnya dan isi dengan air panas secukupnya</li>
                            <li>Mengeluarkan udaranya</li>
                            <li>Menutup buli-buli dengan rapat</li>
                        </ol>
                        Jika kompres hangat basah :
                        <ol type="a">
                            <li>Menyiapkan air hangat kedalam kom</li>
                            <li>Membasahi washlap dengan air hangat</li>
                        </ol>
                    </li>
                    <li>Mendekatkan alat-alat kesisi tempat tidur pasien</li>
                    <li>Posisikan pasien senyaman mungkin</li>
                    <li>Mencuci tangan</li>
                    <li>Memakai sarung tangan</li>
                    <li>Meletakkan perlak/pengalas</li>
                    <li>Membasahi washlap dengan air hangat dan letakkan dikepala
                        pasien</li>
                    <li>Kompres hangat kering diletakkan pada bagian yang nyeri
                        dengan buli-buli hangat dibungkus dengan kain dan sebelum
                        diberikan pada pasien, test alat dengan cara membalikkan alat
                        yaitu posisi tutup berada dibawah</li>
                    <li>Kompres hangat diletakkan dibagian tubuh yang
                        memerlukan (dahi, axila, lipatan paha)</li>
                    <li>Meminta pasien untuk mengungkapkan rasa ketidaknyaman
                        saat dikompres</li>
                    <li>Mengkaji kembali kondisi kulit disekitar pengompresan,
                        hentikan pengompresan jika ditemukan tanda-tanda kemerahan.</li>
                    <li>Merapikan pasien keposisi semula</li>
                    <li>Memberitahu bahwa tindakan sudah selesai</li>
                    <li>Bereskan alat-alat yang telah digunakan dan melepaskan
                        sarung tangan</li>
                    <li>Mencuci tangan</li>
                    <li>Mengkaji respon pasien (respon subjektif dan objektif)</li>
                    <li>Mendokumentasikan pada catatan keperawatan</li>
                </ol>
            </td>
        </tr>
    </table>
