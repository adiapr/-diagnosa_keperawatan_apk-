    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br> Progressive Muscle Relaxation (PMR)</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Indikasi</td>
            <td>
                <ol>
                    <li>Manajemen stres dan ansietas dengan menentukan tanda dan

                        gejala ansietas.</li>
                    <li>Manajemen nyeri pada gangguan fisik dengan meningkatkan
                        vetha endorpin dan berfungsi meningkatkan imun seluler</li>
                    <li>Manajemen insomnia dengan menurunkan gelombang alpa otak.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Untuk</td>
        </tr>
        <tr>
            <td>Persiapan Alat dan Bahan</td>
            <td>
                <ol>
                    <li>Aromaterapi</li>
                    <li>Handuk kecil</li>
                    <li>Air</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Kontra Indikasi</td>
            <td>
                <ol>
                    <li>Cidera akut atau ketidaknyamanan muskuloskeletal, infeksi,

                        inflamsi, dan penyakit berat atau akut.</li>
                    <li>PMR tidak dilakukan pada otot yang sakit.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Teknik Pelaksanaan</td>
            <td>
                <ol>
                    <li>PMR atau relaksasi otot progresif merupakan kontraksi dan
                        relaksasi berbagai kelompok otot mulai dari kaki atas atau dari
                        kepala kearah bawah</li>
                        <li> Pelaksanaan terapi diberikan 2 kali setiap hari selama 2 hari
                        berturut-turut dan total pelaksanaan adalah sebanyak 4 kali.</li>
                        <li>Pelaksanaan gerakan PMR terdiri dari 14 gerakan seperti yang
                        dikembangkan Supriati (2010)</li>
                        <li>Pertemuan pertama terapis melakukan role play terlebih dulu
                        dan bimbingan kepada reponden/ konseli sampai konseli
                        memahami dan mampu melakukan 14 gerakan dalam PMR.</li>
                        <li>Setiap gerakan terdiri dari role model, role play, feedback dan
                        transfer training. Pertemuan kedua sampai keempat, terapais tidak
                        melakukan secara langsung, tetapi reponden/konseli mengikuti
                        gerakan terapi dengan panduan CD yang sudah disiapkan oleh
                        terapis.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Waktu dan Tempat</td>
            <td>
                <ol>
                    <li>Dilakukan 2 jam setelah reponden/ konseli makan untuk

                        mencegahrasa mengantuk.</li>
                    <li>PMR dilakukan sebaiknya diberikan pada tempat nyaman dan
                        tidakramai.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Alat bantu dan
                Persiapan</td>
            <td>
                <ol>
                    <li>Musik terapi.</li>
                    <li>Diri terapis dan kemampuan untuk dapat melakukan PMR.</li>
                    <li>Tempat duduk atau tempat tidur</li>
                    <li>Leaflet.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Langkah-langkah kegiatan :</td>
            <td>
                <ol>
                    <li>Fase Orientasi
                        <ol type="a">
                            <li>Salam terapeutik</li>
                            <li>Perkenalan diri terapis dengan menyebutkan nama lengkap dan
                                nama panggilan</li>
                        </ol>
                    </li>
                    <li>Validasi
                        <ol type="a">
                            <li>Tanya perasaan reponden dan kesiapan reponden
                                mengikuti terapi.</li>
                            <li>Tanyakan ketegangan otot yang dirasakan oleh reponden.</li>
                        </ol>
                    </li>
                    <li>Kontrak
                        <ol type="a">
                            <li>Jelaskan jumlah sesi pertemuan yang harus diikuti yaitu 4
                                kali pertemuan dilakukan 2 hari setiap hari.</li>
                            <li>Waktu : 30-45 menit</li>
                            <li>Tempat: Ruangan yang tenang</li>
                            <li>Tujuan pertemuan : reponden mampu melakukan gerakan
                                PMR yang terdiri Dari 14 gerakan dengan bimbingan
                                trapis.</li>
                        </ol>
                    </li>
                    <li>Fase Kerja
                        <ol type="a">
                            <li>Minta reponden untuk melepaskan kaca mata dan jam
                                tangan, melonggarkan Ikat pinggang dan pakaian yang
                                ketat.</li>
                            <li>Mempersilahkan reponden duduk dan tenang pada posisi
                                berbaring di tempat tidur pada posisi yang nyaman.</li>
                            <li>Mejelaskan PMR mulai dari pengertian, tujuan dan proses
                                pelaksanaan yang terdiri dari prosedur umum dan gerakan
                                inti.</li>
                            <li>Meinta reponden untuk mempertahankan mata terbuka
                                selama beberapa menit. Kemudian secara berlahan
                                menutup mata dan mempertahankannya tetap tertutup.</li>
                            <li>Meminta reponden untuk tarik napas dalam, dalam
                                beberapa kali sebelum memulai latihan dengan cara nafas
                                dalam secara perlahan-lahan melalui hidung dan
                                hembuskan keluar melalui mulut 1 kali.</li>
                            <li>Melanjutkan dengan 14 gerakan initi mulai dari otot
                                tangan belakang, otot bisep, otot bahu, otot dahi, otot mata,
                                otot rahang, otot mulut, otot leher depan, dan belakang,
                                otot punggung, otot dada, otot perut, otot kaki dan paha.</li>
                        </ol>
                    </li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Langkah langkah PMR</td>
            <td>
                <ol>
                    <li>Terapis memodelkan/mendemonstrasikan gerakan ke-1 yaitu
                        genggam tangan dengan membuat kepalan selama 5-7 detik, dan
                        rasakan ketegangan yang terjadi kemudian dilepaskan selama 10
                        detik. Melakukan gerakan sebanyak 2 kali</li>
                    <li>Terapis memodelkan/mendemonstrasikan gerkan ke-2 yaitu
                        menekuk kebelakang pergelangan tangan sehingga otot-otot
                        ditangan bagian belakang dan bagian bawah menegang ke
                        langit-langit selama 5 detik, dan dilepaskan selama 10 detik.
                        Kemudian ulangi sekali lagi.</li>
                    <li>Terapis memodelkan/mendemonstrasikan gerkan ke-3 yaitu
                        menggenggam tangan sehingga menjadi kepalan ke pundak
                        selama 5 detik. Rasakan ketagannya kemudian lepaskan selama 10
                        detik. Ulangi sekali lagi.</li>
                    <li>Melatih gerakan ke-4 yaitu mengangkat kedua bahu
                        setinggi-tingginya seakan-akan bahu akan dibawa hingga
                        menyentuh kedua telinga selama 5 detik, kemudian lepaskan
                        selama 10 detik. Ulangi sekali lagi.</li>
                    <li>Melakukan gerakan ke-5 sampai dengan ke-8 yaitu gerakan
                        yang ditujukan untuk melemaskan otot-otot di wajah (dahi, mata,
                        rahang, dan mulut) pertama kerutkan dahi dan alis sampai
                        otot-ototnya terasa dan kulitnya keriput. Lakukan selama 5 detik
                        kemudian lepaskan selama 10 detik. Ulangi sekali lagi.</li>
                    <li>Tutup keras-keras mata sampai mata terasa tegangannya selama
                        5 detik kemudian lepaskan selama 10 detik. Ulangi sekali lagi.</li>
                    <li>Katupkan rahang dengan menggigit gigi-gigi dengan kuat
                        selama 5 detik kemudian lepaskan selama 10 detik kemudian
                        ulangi gerakan sekali lagi.</li>
                    <li>Moncongkan mulut sekuat-kuatnya sehingga terasa ketegangan
                        disekitar mulut selama 5 detik kemudian lepaskan selama 10 detik dan ulangi gerakan sekali lagi.</li>
                    <li>Melatih gerakan ke 9 dan 10 Tekankan kepala kepermukaan
                        bantalan kursi atau ketempat tidur sehingga dapat merasakan
                        ketegangan dibelakang leher dan punggung atas kemudian
                        rilekskan. Ulangi sekali lagi.</li>
                    <li>Melatih gerkaan ke-11 yaitu mengangkat tubuh dari sandaran
                        kursi atau tempat tidur. Kemudian punggung dilengkungkan dan
                        dada dibusungkan selama 5 detik kemudian lepaskan selama 10
                        detik. Ulangi sekali lagi.</li>
                    <li>Melatih gerakan ke-12 yaitu menarik napas panjang dan dalam
                        untuk mengisi paru- paru dengan udara sebanyak-banyaknya.
                        Ulangi sekali lagi.</li>
                    <li>Melatih gerakan ke-13 yaitu menarik kuat-kuat perut ke dalam
                        kemudian tahan selama 5 detik sampai perut menjadi kencang dan
                        keras. Lepaskan selama 10 detik</li>
                    <li>Melatih gerakan ke-14 yaitu menarik kuat-kuat perut kedalam
                        kemudian tahan selama 5 detik sampai perut menjadi kencang dan
                        keras. Lepaskan selama 10 detik dan ulangi sekali lagi.</li>
                </ol>
            </td>
        </tr>
