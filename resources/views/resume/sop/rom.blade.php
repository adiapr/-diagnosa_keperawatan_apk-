    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   ROM AKTIF / PASIF</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Latihan gerak aktif-pasif atau range of motion (ROM) adalah 
                latihan yang dilakukan untuk mempertahankan atau 
                memperbaiki tingkat kesempurnaan kemampuan menggerakkan 
                persendian secara normal dan lengkap
                </td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>
                <ol>
                    <li>Untuk mengurangi kekakuan pada sendi dan kelemahan 
                        pada otot yang dapat dilakukan secara aktif maupun pasif 
                        tergantung dengan keadaan pasien.
                        </li>
                    <li>Meningkatkan atau mempertahankan fleksibilitas dan 
                        kekuatan otot
                        </li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Indikasi</td>
            <td>
                <ol>
                    <li>Pasien yang mengalami hambatan mobilitas fisik</li>
                    <li> Pasien yang mengalami keterbatasan rentang gerak</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Klien</td>
            <td>
                <ol>
                    <li>Menjelaskan pada klien mengenai tujuan dari tindakan keperawatan yang diberikan kepada klien </li>    
                    <li>Kaji kembali keluhan klien dan disesuaikan dengan indikasi, serta rencana tindakan yang ingin diberikan kepada klien </li>    
                    <li>Kontrak waktu dengan klien : kapan pelaksanaan dan berapa lama pelaksanaan tindakan keperawatan </li>    
                </ol>     
            </td>
        </tr>
        <tr>
            <td>Pelaksanaan</td>
            <td>
                Fase Orientasi
                <ol>
                    <li>Mengucapkan salam kepada klien </li>
                    <li>Mengidendifikasi klien dengan ; nama, tanggal lahir, nomor rekam medis (nrm) klien </li>
                    <li>Memvalidasi kontrak waktu yang telah disepakati </li>
                </ol>
                Fase Kerja
                <ol start="4">
                    <li> Menutup sampiran/gordyn </li>
                    <li>Mencuci tangan </li>
                    <li>Latihan ROM lengan
                        <ol type="a">
                            <li>Lengan bawah fleksi, sehingga telapak tangan dan jari jari tangan pada posisi vertikal.</li>
                            <li>Lakukan gerak fleksi kedepan pada pergelangan.</li>
                            <li>Lakukan gerakan fleksi ke belakang pada pergelangan tangan pasien.</li>
                            <li></li>
                        </ol>
                    </li>
                    <li>Latihan ROM siku
                        <ol type="a">
                            <li>Posisi tangan pasien sejajar dengan tubuh dengan telapak tangan menghadap keatas.</li>
                            <li>Lakukan gerakan fleksi siku dengan mengangkat lengan bawah kearah atas.</li>
                            <li>Kembalikan ke posisi semula dan ulangi sebanyak 8 kali.</li>
                        </ol>
                    </li>
                    <li>Latihan pronasi lengan bawah
                        <ol type="a">
                            <li>Perawat memutar lengan bawah pasien kearah luar atau ke arah perawat.</li>
                            <li>Kembalikan ke posisi semula.</li>
                            <li>Lakukan gerakan supinasi, perawat memutar lengan bawah pasien kearah dalam.</li>
                            <li>Kembalikan ke posisi semula, lakukan gerakan 8 kali.</li>
                        </ol>
                    </li>
                    <li>Latihan fleksi dan ekstensi bahu
                        <ol type="a">
                            <li>Mengangkat lengan klien ke atas sehingga posisi tangan klien tegak lurus.</li>
                            <li>Kembalikan ke posisi semula dan lakukan sebanyak 8 kali.</li>
                        </ol>
                    </li>
                    <li>Abduksi dan adduksi bahu
                        <ol type="a">
                            <li>Posisi lengan pasien sejajar dengan tubuh telapak tangan menghadap keatas.</li>
                            <li>Gerakkan tangan menjauhi tubuh, kembalikan ke posisi semula, ulangi 
                                gerakan 8 kali.
                            </li>
                        </ol>
                    </li>
                    <li>Latihan rotasi bahu
                        <ol type="a">
                            <li>Posisi lengan kanan bawah pasien tegak lurus dengan lengan fleksi.</li>
                            <li>Gerakkan lengan bawah ke arah depan atau ke bawah sehingga menyentuh tempat tidur.</li>
                            <li>Kembalikan ke possi semula.</li>
                            <li>Perawat menggerakkan lengan bawah ke belakang sampai punggung tangan menyentuh tempat tidur. </li>
                            <li>Ulangi gerakan 8 kali.</li>
                        </ol>
                    </li>
                    <li>Latihan fleksi dan ekstensi jari kaki
                        <ol type="a">
                            <li>Posisi kaki pasien lurus. </li>
                            <li>Lakukan gerakan fleksi jari jari ke depan  atau jari jari menghadap ke arah permukaan tempat tidur.</li>
                            <li>Lakukan gerakan ekstensi jari dengan menggerakkan jari jari kebelakang.</li>
                            <li>Ulangi kegiatan 8 kali</li>
                        </ol>    
                    </li>
                    <li>Latihan inversi dan eversi kaki
                        <ol type="a">
                            <li>Posisikan kaki klien dalam posisi ekstensi.</li>
                            <li>Gerakkan kaki klien kearah dalam sehingga telapak kaki menghadap kearah kaki lainnya.</li>
                            <li>Kembalikan ke posisi semula.</li>
                            <li>Lakukan gerakan memutar kearah luar sehingga telapak kaki menjauhi kaki lainnya.</li>
                            <li>Kembalikan ke posisi semula.</li>
                            <li>Ulangi sebanyak 8 kali.</li>
                        </ol>
                    </li>
                    <li>Latihan fleksi dan ekstensi lutut
                        <ol type="a">
                            <li>Perawat mengangkat kaki klien dengan tinggi 8 cm kemudian tekuk lutut kearah dada.</li>
                            <li>Lakukan gerakan ekstensi lutut, dengan menurunkan kaki klien kebawah dan kembalikan ke posisi semula.</li>
                            <li>Ulangi kegiatan sebanyak 8 kali.</li>
                        </ol>
                    </li>
                    <li>Latihan adduksi dan abduksi pangkal paha
                        <ol type="a">
                            <li>Perawat mengangkat kaki klien dengan tinggi 8 cm.</li>
                            <li>Angkat kaki klien kearah samping menjauhi tubuh.</li>
                            <li>Lakukan gerakan abduksi dengan mengangkat kaki dan arahkan mendekati tubuh.</li>
                            <li>Ulangi sampai 8 kali.</li>
                        </ol>
                    </li>
                </ol>
                Fase Terminasi 
                <ol start="17">
                    <li>Mencuci tangan </li>
                    <li>Mengevaluasi perasaan klien  setelah dilakukannya tindakan keperawatan </li>
                    <li> Mengevaluasi secara subyektif aadanya perubahan saat atau 
                        setelah dilakukan tindakan keperawatan 
                            </li>
                    <li>Kontak waktu untuk tindak lanjut untuk tindakan keperawatan selanjutnya</li>
                    <li>Mengucapkan salam kepada klien </li>
                    <li>Mendokumentasikan tindakan keperawatan yang telah diberikan kepada klien (di ners station)</li>

                </ol>
            </td>
        </tr>
        <tr>
            <td>Evaluasi</td>
            <td>
                <ol type="a">
                    <li> Respon
                        <ul>
                            <li>Respon verbal: klien mengatakan tidak kaku lagi</li>
                            <li>Respon non verbal: klien tidak terlihat sulit untuk menggerakkan sisi tubuhnya yang kaku.</li>    
                        </ul>    
                    </li>
                    <li>Beri reinforcement positif</li>
                    <li>Lakukan kontrak untuk kegiatan selanjutnya</li>
                    <li>Mengakhiri kegiatan dengan baik</li>
                </ol>
            </td>
        </tr>
    </table>
