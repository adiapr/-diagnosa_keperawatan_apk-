    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Melakukan tindakan penghisapan lendir di jalan nafas</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>
                <ol>
                    <li>Mengeluarkan secret/cairan pada jalan nafas</li>        
                    <li>Melancarkan jalan nafas</li>        
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur</td>
            <td>
                Persiapan Persiapan alat
                <ol type="a">
                    <li>Bak instrument berisi: pinset anatomi 2, kasa secukupnya    </li>
                    <li>NaCl atau air matang</li>
                    <li>Canule suction</li>
                    <li>Perlak dan pengalas                    </li>
                    <li>Mesin suction</li>
                    <li>Kertas tissue</li>
                </ol>
                Persiapan pasien dan lingkungan
            </td>
        </tr>
        <tr>
            <td>Pelaksanaan</td>
            <td>
                <ol>
                    <li>Tahap PraInteraksi
                        <ol type="a">
                            <li>Mengecek program terapi</li>    
                            <li>Mencuci tangan</li>    
                            <li>Menyiapkan alat</li>    
                        </ol>    
                    </li>    
                    <li>Tahap Orientasi
                        <ol type="a">
                            <li>Memberikan salam dan sapa nama pasien</li>    
                            <li>Menjelaskan tujuan dan prosedur pelaksanaan</li>    
                            <li>Menanyakan persetujuan/kesiapan pasien</li>    
                        </ol>
                    </li>    
                    <li>Tahap Kerja
                        <ol type="a">
                            <li>Memberikan posisi yang nyaman pada pasien kepala sedikit Ekstensi</li>    
                            <li>Memberikan Oksigen 2 – 5 menit</li>    
                            <li>Meletakkan pengalas di bawah dagu pasien</li>    
                            <li>Memakai sarung tangan</li>    
                            <li>Menghidupkan mesin, mengecek tekanan dan botol penampung</li>    
                            <li>Memasukkan kanul section dengan hati-hati (hidung ± 5 cm, mulut ±10 cm)</li>    
                            <li>Menghisap lendir dengan menutup lubang kanul, menarik keluar perlahan sambil memutar (+ 5 detik untuk anak, + 10 detik untuk dewasa)</li>    
                            <li>Membilas kanul dengan NaCl, berikan kesempatan pasien bernafas</li>    
                            <li>Mengulangi prosedur tersebut 3-5 kali suctioning</li>    
                            <li>Mengobservasi keadaan umum pasien dan status pernafasannya</li>    
                            <li>Mengobservasi secret tentang warna, baud an volumenya</li>    
                        </ol>    
                    </li>    
                    <li>Tahap Terminasi
                        <ol type="a">
                            <li>Mengevaluasi tindakan yang baru dilakukan</li>    
                            <li>Merapikan pasien dan lingkungan</li>    
                            <li> Berpamitan dengan pasien</li>    
                            <li>Membereskan dan kembalikan alat ketempat semula</li>    
                            <li>Mencuci tangan</li>    
                            <li>Mencatat kegiatan dalam lembar catatan keperawatan</li>    
                        </ol>    
                    </li>    
                </ol>    
            </td>
        </tr>
