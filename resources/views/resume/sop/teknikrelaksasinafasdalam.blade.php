    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br> TEKNIK RELAKSASI NAFAS DALAM</b>
    </b></h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Menurut Smeltzer & Bare (2002) dalam Andarmoyo (2013) relaksasi nafas
                dalam adalah suatu tindakan untuk membebaskan mental dan fisik dari
                ketegangan dan stress sehingga dapat meningkatkan toleransi terhadap efek
                nyeri yang dirasakan.</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Untuk dapat mengurangi/menghilangkan rasa nyeri</td>
        </tr>
        <tr>
            <td>Persiapan</td>
            <td>
                Tahap pra interaksi :
                <ol type="a">
                    <li>Cek catatan keperawatan</li>
                    <li>Siapkan alat-alat</li>
                    <li>Cuci tangan</li>
                </ol>
                Tahap orientasi :
                <ol type="a">
                    <li>Berikan salam, panggil klien dengan nama</li>
                    <li>Menjaga privacy klien</li>
                    <li>Jelaskan tujuan, prosedur dan lamanya tindakan kepada klien dan keluarga</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur</td>
            <td>
                Menurut Nurdin dkk (2013), prosedur teknik relaksasi nafas dalam adalah sebagai berikut :
                <ol>
                    <li>Ciptakan lingkungan yang tenang</li>
                    <li>Usahakan tetap rileks dan tenang</li>
                    <li>Menarik nafas dalam dari hidung dan mengisi paru-paru dengan udara
                        melalui hitungan 1.2.3</li>
                    <li>Perlahan-lahan udara dihembuskan melalui mulut sambil merasakan
                        ekstrimitas atas dan bawah rileks.</li>
                    <li>Anjurkan bernafas dengan irama normal 3 kali</li>
                    <li>Menarik nafas lagi melalui hidung dan menghembuskan melalui mulut
                        secara perlahan-lahan</li>
                    <li>Membiarkan telapak tangan dan kaki rileks</li>
                    <li>Usahakan agar tetap konsentrasi/mata sambil terpejam</li>
                    <li>Pada saat konsentrasi pusatkan pada daerah yang nyeri</li>
                    <li>Anjurkan untuk mengulangi prosedur hingga nyeri terasa berkurang/li>
                    <li>Ulangi sampai 15 kali, dengan selingi istirahat singkat setiap 5 kali. Bila
                        nyeri menjadi hebat, seseorang dapat bernafas secara dangkal dan cepat.</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Terminasi</td>
            <td>
                <ol>
                    <li>Evaluasi hasil kegiatan</li>
                    <li>Lakukan kontrak untuk kegiatan selanjutnya</li>
                    <li>Akhiri kegiatan dengan baik</li>
                    <li>Cuci tangan</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Dokumentasi</td>
            <td>
                <ol>
                    <li>Catat waktu pelaksanaan tindakan</li>
                    <li>Catat respons pasien</li>
                    <li>Paraf dan nama perawat jaga</li>
                </ol>
            </td>
        </tr>
    </table>
