        <b>STANDAR OPERASIONAL PROSEDUR  <br> TERAPI AKUPUNTUR</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Akupuntur merupakan pengobatan yang dilakukan dengan cara
                menusukkan jarum di titik-titik tertentu pada tubuh pasien yaitu
                telinga, kepala, sekitar telapak kaki dan tangan untuk
                mempengaruhi/memperbaiki kesalahan aliran bioenergi tubuh yang
                disebut dengan Qi (dibaca: Chi) (Djuharto, 1982).</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Mengembalikan keseimbangan energi vital (homeostasis), serta
                mengoptimalkan terbentuknya antibodi pada tubuh pasien dengan
                adanya aliran Qi yang seimbang sehingga gangguan kesehatan dapat
                teratasi (Saputra Agustin, 2005).</td>
        </tr>
        <tr>
            <td>Indikasi</td>
            <td>
                <ol>
                    <li>Saluran napas; berbagai radang yang ditunjukanuntuk
                        mengatasi kondisi alergi dan meningkatkan daya tahan tubuh.</li>
                    <li>Mata; kelainan mata yang bersifat radang dan fungsional otot
                        serta refraksi.</li>
                    <li>Mulut; untuk penanggulangan nyeri dalam peradangan kronis.</li>
                    <li>Saluran makanan dan lambung; berbagai kelainan fungsional
                        yaitu otot, ekskresi asam lambung, nyeri dan peradangan.</li>
                    <li>Saraf, otot, dan tulang; yaitu masalah nyeri,kelemahan dan
                        kelumpuhan serta peradangan persendian</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Kontradiksi</td>
            <td>
                <ol>
                    <li>Penderita dalam keadaan hamil</li>
                    <li>Penderita yang memakai pacu jantung</li>
                    <li>Menusuk dekat daerah tumor ganas</li>
                    <li>Menusuk pada kulit yang sedang meradang</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Pasien</td>
            <td>
                <ol>
                    <li>Pastikan identitas klien</li>
                    <li>Kaji kondisi klien terakhir</li>
                    <li>Beritahu dan jelaskan pada klien atau keluarganya
                        tentang tindakan yg dilakukan</li>
                    <li>Jaga privasi klien</li>
                    <li>Posisikan klien senyaman mungkin</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Alat</td>
            <td>
                <ol>
                    <li>Jarum (ukuran jarum: 0,5 cun, 1cun, 1,5 cun)</li>
                    <li>Bed/tempat tidur yang datar</li>
                    <li>Sarung tangan (bila perlu)</li>
                    <li>Kapas alkohol</li>
                    <li>Bengkok</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur</td>
            <td>
                Tahap Orientasi
                <ol>
                    <li>Berikan salam, panggil klien dengan namanya (kesukaannya)</li>
                    <li>Perkenalkan nama dan tanggung jawab perawat</li>
                    <li>Jelaskan tujuan, prosedur dan lamanya tindakan pada klien/keluarga</li>
                    <li>Berikan kesempatan kepada klien/keluarga untuk bertanya sebelum
                        terapi dilakukan</li>
                </ol>
                <br>
                Tahap Kerja
                <ol>
                    <li>Jaga privasi klien dengan menutup tirai</li>
                    <li>Atur posisi klien dengan memposisikan klien pada posisi telentang
                        (supine), duduk, duduk dengan tangan bertumpu di meja, berbaring
                        miring atau tengkurap dan berikan alas</li>
                    <li>Bantu melepaskan pakaian klien atau aksesoris yang dapat
                        mennghambat tindakan akupunktur yang akan dilakukan, jika perlu</li>
                    <li>Cuci tangan dan gunakan sarung tangan bila perlu</li>
                    <li>Bersihkan (desinfeksi) daerah yang akan ditusukkan jarum dengan
                        kapas alkohol
                        <ol type="a">
                            <li>Ambil jarum sesuai ukuran ( 0,5 cun: wajah; 1 cun: lengan; 1,5 cun:
                                bokong) ukuran jarum disesuaikan dengan ketebalan kulit</li>
                            <li>Jika menggunakan alat bantu masukkan jarum ke dalam alat bantu
                                dan dekatkan dengan kulit untuk ditusukkan. Alat bantu biasanya
                                berupa tabung kecil yang terbuat dari bahan plastik seperti sedotan</li>
                            <li>Jika tanpa batuan alat atau jari tangan telanjang:
                                <ul>
                                    <li>Jika jarum tebal: Jari salah satu tangan memegang bagian pegangan
                                        jarum, arahkan mata jarum pada titik akupuntur terpilih, dan tusukkan
                                        dengan teknik tertentu (tegak lurus, menyudut, sejajar dan lain-lain)</li>
                                    <li>Jika jarum tipis: Jari salah satu tangan memegang pegangan jarum
                                        dan tangan lainnya memegang batang jarum sebagai pengarah mata
                                        jarum dan penunjang jarum</li>
                                    <li>Jika jarum berukuran kecil: Jari telunjuk dan ibu jari menjepit batang

                                        jarum (dekat mata jarum), jarum), kemudian kemudian jarum
                                        ditusukkan ditusukkan dengan cara “memegaskan” jari telunjuk dan
                                        jempol tersebut.</li>
                                </ul>
                            </li>
                        </ol>
                    </li>
                    <li>Tanyakan perasaan klien setelah ditusukkan jarum, apakah sudah
                        merasa nyaman/belum</li>
                    <li>Diamkan jarum di tempat penusukkan selama 15-20 menit</li>
                    <li>Setelah sesi terapi selama 15-20 menit, cabut jarum dan desinfeksi
                        dengan kapas alkohol</li>
                </ol>
                <br>
                Terminasi
                <ol>
                    <li>Jelaskan pada klien bahwa terapi sudah selesai dilakukan</li>
                    <li>Kaji respon klien setelah dilakukan terapi</li>
                    <li>Berikan reinforcement positif kepada klien</li>
                    <li>Rapikan pakaian klien dan kembalikan ke posisi yang nyaman</li>
                    <li>Rapikan alat-alat</li>
                    <li>Cuci tangan</li>
                </ol>
            </td>
        </tr>
