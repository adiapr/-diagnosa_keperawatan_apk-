    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   TERAPI KELUARGA</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Keluarga merupakan sistem pendukung utama yang memberi peawatan langsung pada setiap keadaan ( sehat-sakit) klien Terapi keluarga meupakan suatu psikoterapi modalitas dengan focus pada penanganan keluarga sebagai unit sehingga dalam pelaksanaanya terapis membantu keluarga dalam mengidentifikasi dalam
                perbaikan keadaan yang maladaptive.
                </td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>
                <ol>
                    <li>Menurunkan konflik, kecemasan keluarga kepada pasien
                    </li>
                    <li>Meningkatkan kesadaran keluarga terhadap kebutuhan masingmaasing anggota keluarga kepada pasien</li>
                    <li>Meningkatkan kemampuan penanganan terhadap krisis kepada pasien
                    </li>
                    <li>Mengembangkan hubungan	peran yang sesuai kepada pasien</li>
                    <li>Meningkatkan kesehatan jiwa keluarga sesuai dengan
                        tingkat perkembangan anggota keluarga kepada pasien
                        </li> 
                </ol>
            </td>
        </tr>
        <tr>
            <td>Indikasi</td>
            <td>Dilakukan pada pasien dengan gangguan psikososial: Masalah
                keluarga
                </td>
        </tr>
        <tr>
            <td>Persiapan</td>
            <td>
                <ol>
                    <li>Alas tempat duduk</li>
                    <li>Ruangan yang nyaman dan tenang</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Prosedur</td>
            <td>
                PRAINTERAKSI
                <ol>
                    <li>Menyiapkan diri secara fisik dan psikologis (tidak ada konflik internal yang dapat mempengaruhi proses terapi)</li>
                    <li>Mempelajari rekam medis pasien sebagai data awal</li>
                    <li>Menyiapkan lingkungan yang tenang, nyaman, dan aman                    </li>
                </ol>

                TAHAP KERJA
                <ol>
                    <li>	Meminta kepada klien dan keluarga duduk setengah lingkaran</li>
                    <li>Melatih komunikasi, menyelesaikan konflik, mengatasi prilaku dan stress</li>
                    <li>Memberikan kesempatan kepada klien untuk memvalidasi perasaan dan pengalaman</li>
                    <li>Meminta kepada klien untuk mengungkapkan masalahnya  </li>
                    <li>Meminta keluarga membuat sesuatu keadaan dimana anggota keluarga dapat melihat bahaya terhadap diri klien dan aktivitasnya.</li>
                    <li>Meminta klien mengidentifikasi keluhan klien yang dirasakan sebagai masalah</li>
                    <li>Meminat klien dan keluarga mengindentifikasi harapan klien dan keluarganya terhadap terapi keluarga</li>
                    <li>Meminta kepada keluarga mengubah cara berpikir klien ( Reframing)</li>
                    <li>Meminta klien tidak merasa takut dan bersikap terbuka                    </li> 
                </ol>
            </td>
        </tr>
