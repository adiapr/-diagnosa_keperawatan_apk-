    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   TERAPI KOGNITIF</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Salah satu teknik terapi kognitif prilaku yang dilakukan perawat untuk membantu pasien menghentikan pikiran negatifnya.</td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>Pasien terbebas dari pikiran negative atau pikiran yang menyimpang sehingga prilakunya adaptif.</td>
        </tr>
        <tr>
            <td>Indikasi</td>
            <td>Semua pasien yang memiliki pikiran negative atau pikiran menyimpang dan mengganggu prilaku(maladaptif) Misalnya: Over Generalization, Filter Mental, Loncatan dan lain-lain.</td>
        </tr>
        <tr>
            <td>Persiapan Alat</td>
            <td>
                <ol>
                    <li>Kursi yang ada sandaran kepala dan tangan( 1 perawat- 1 pasien)</li>
                    <li>Dokumen distorsi kognitif</li>
                </ol>    
            </td>
        </tr>
        <tr>
            <td>Persiapan Perawat</td>
            <td>
                <ol>
                    <li> Memastikan Distorsi Pikiran</li>
                    <li>Memastikan kebutuhan terapi</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan Lingkungan</td>
            <td>Ruangan yang tenang dan nyaman <br>
                Tertutup (minimalisir stimulus)
                </td>
        </tr>
        <tr>
            <td>Prosedur Kerja</td>
            <td>
                <ol>
                    <li>Menyampaikan salam</li>
                    <li>Mengingatkan nama perawat</li>
                    <li>Menegaskan kembali kontrak untuk terapi</li>
                    <li>Menyampaikan tujuan terapi</li>
                    <li>Menanyakan kesiapan pasien untuk terapi</li>
                    <li>Menyiapkan kursi/mengambil tempat*</li>
                    <li>Memberikan kesempatan pasien untuk bak/bab (k/p)</li>
                    <li>Menanyakan keluhan utama/memberi kesempatan pasien bertanya/menyampaikan sesuatu (k/p 
                        tindaklanjuti sementara)
                        </li>
                    <li>Menjelaskan prosedur terapi sekaligus memperagakan</li>
                    <li>Membimbing pasien melakukan perasat:
                        <ol type="a">
                            <li>Letakkan tubuh pasien dan semua anggota badan termasuk kepala (bersandar) pada kursi senyaman 
                                mungkin.
                                </li>
                            <li>Tutup mata</li>
                            <li>Ambil nafas melalui hidung (secukupnya) tahan sebentar, keluarkan melalui mulut perlahanlahan(Lakukan sampai merasa tenang)</li>
                            <li>Minta pasien untuk menghadirkan pikiran-pikiran yang tidak menyenangkan/menyakitkan yang telah 
                                disepakati u/ dihentikan. (Diawali dari hal positif-negatif/menyenangkan-menyakitkan)
                                </li>
                            <li>Pastikan pasien mampu menghadirkan (perhatikan responnya)</li>
                            <li>Minta pasien untuk mengatakan pada dirinya “ STOP!” (Dengan penuh kesungguhan)</li>
                            <li>Buka mata</li>
                        </ol>
                    </li>
                    <li>Tanyakan/evaluasi respon pasien</li>
                    <li> Kesimpulan dan support (Telah melakukan dengan baik dan mampu menerapkannya)</li>
                    <li>Memberikan follow up, apa yg harus dilakukan selanjutnya.(Terapkan dalam kehidupan sehari-hari 
                        apabila datang lagi pikiran seperti itu)
                        </li>
                    <li> Salam terapeutik</li>
                </ol>
            </td>
        </tr> 
    </table>
