    <h5 align="center">
        <b>STANDAR OPERASIONAL PROSEDUR  <br>   TERAPI REMINISCENCE</b>
    </h5>

    <table width="100%" class="table table-bordered">
        <tr>
            <td>Pengertian</td>
            <td>Terapi rereminiscence atau terapi kenangan sebagai terapi pendekatan kognitif 
                untuk mengingat memori menyenangkan yang terjadi di masa lalu untuk disampaikan
                ke keluarga atau orang terdekat dengan menggunakan stimulus seperti benda yang
                 berhubungan dengan masa lalu klien.
                </td>
        </tr>
        <tr>
            <td>Tujuan</td>
            <td>
                <ol type="a">
                    <li>Meningkatkan  harga diri</li>
                    <li>Meningkatkan perasaan berharga</li>
                    <li>Beradaptasi terhadap stres</li>
                    <li>Meningkatkan kemampuan komunikasi</li>
                    <li>Meningkatkan sosialisasi</li>
                    <li>Meningkatkan kualitas hidup</li>
                </ol>
            </td>
        </tr>
        <tr>
            <td>Persiapan (alat, bahan, lingkungan) </td>
            <td>
                Bahan: Benda-benda yang masih dimiliki pasien yang berkaitan
                    engan masa lalu seperti: foto, permainan, CD, majalah, buku atau koleksi lainnya. <br>
                    Lingkungan: Menyiapkan kondisi lingkungan yang kondusif, ruangan yang aman dan nyaman.

            </td>
        </tr>
        <tr>
            <td>Prosedur Pelaksanaan</td>
            <td>
                Proses pelaksanaan terapi ini diadaptasi dari penelitian yang dilakukan oleh

                Syariah (2010) yang dimodifikasi dari terapi reminiscence yang telah dilakukan olehStinson (2009), Parese, Simon & Ryan (2008) dan Jones (2003). Terapi reminiscence ini terdiri dari 5 sesi sebagai berikut: 
                <br> <br>
                Sesi-1 <br>
                Berbagi pengalaman masa anak
                <ul>
                    <li>Perkenalan fasilitator sebagai leader dan semua anggota kelompok.</li>
                    <li>Fasilitator memberikan penjelasan mengenai maksud dan tujuan terapi reminiscence.</li>
                    <li>Berbagi pengalaman yang paling menyenangkan pada masa anak berkaitan dengan teman yang paling disenangi.</li>
                    <li>Persilahkan pasien untuk menceritakan tentang hari - harinya di sekolah.</li>
                    <li>Diskusikan tentang mainan favorit (paling disenangi)</li>
                    <li>Berikan permainan yang berhubungan dengan masa lalu pasien dan beri kesempatan pada pasien untuk memainkannya</li>
                </ul>

                Sesi-2 <br>
                Berbagi pengalaman masa remaja
                <ul>
                    <li>Berbagi pengalaman tentang hobi yang paling menyenangkan yang dilakukan bersama teman sebaya sewaktu remaja.</li>
                    <li>Berbagi pengalaman tentang rekreasi yang paling menyenangkan yang dilakukan. </li>
                    <li>Diskusikan tentang teman-teman terdekat pada waktu masa remaja.</li>
                    <li>Ingatkan masa lalu melalui lagu-lagu </li>
                    <li>Berikan kesempatan kepada anggota untuk menceritakan tentang lagu tersebut yang mungkin mempunyai arti khusus bagi dirinya.</li>
                    <li>Dorong untuk bertepuk tangan dan menyanyi.</li>
                </ul>

                Sesi-3
                Berbagi pengalaman masa dewasa <br>
                <ul>
                    <li>Berbagi pengalaman tentang pekerjaan yang paling menyenangkan yang dilakukan pada usia dewasa.</li>
                    <li>Berbagi pengalaman yang menyenangkan tentang makanan yang paling disukai pada waktu usia dewasa.</li>
                    <li>Ingatkan tentang tanggal penikahan. Persilahkan pasien untuk membawa foto-foto pernikahan. </li>
                    <li>Dorong pasien untuk membawa sesuatu yang patut. dikenang dari karir atau pekerjaan mereka sewaktu dewasa dan menceritakannya.</li>
                </ul>

                Sesi-4
                Berbagi pengalaman keluarga dan di rumah
                <ul>
                    <li>Diskusikan tentang pekerjaan/kehidupan dirumah atau kegiatan sukarela pekerjaan pertama.</li>
                    <li>Ingatkan tentang anak-anak, keluarga dan binatang kesayangan. </li>
                    <li>Menceritakan tentang anggota keluarga dan makanan yang paling disukai di rumah. </li>
                    <li>Berbagi pengalaman yang paling menyenangkan pada saat merayakan hari raya agama beserta</li>
                    <li>Berbagi pengalaman tentang tetangga yang paling disukai</li>
                    <li>Dorong para anggota untuk menunjukkan foto-foto keluarga dan menceritakan pengalaman pribadi yang patut dikenang. </li>
                </ul>

                Sesi-5
                Evaluasi integritas diri 
                <ul>
                    <li>Menyampaikan perasaan setelah mengikuti kegiatan terapi reminiscence dari sesi 1-4. </li>
                    <li>Menyampaikan manfaat yang dicapai (dimasakan) setelah mengikuti kegiatan terapi reminiscence sampai selesai. </li>
                    <li>Menyampaikan harapan dan rencana kegiatan setelah terapi selesai.</li>
                </ul>




            </td>
        </tr>  
