                    <li>Alat mewarnai</li>
                </ol>
            </td>
        </tr> 
        <tr>
            <td>Prosedur</td>
            <td>
                <b>Pre Interaksi</b>
                <ol start="1">
                    <li>Cek catatan keperawatan atau catatan medis klien (jika ada)</li>
                    <li>Siapkan alat-alat</li>
                    <li>Identifikasi faktor atau kondisi yang dapat menyebabkan kontra indikasi</li>
                    <li>Cuci tangan</li>
                </ol>
                <b>Tahap Orientasi</b>
                <ol start="5">
                    <li>Beri salam dan panggil klien dengan namanya. Bina hubungan saling percaya.</li>
                    <li>Jelaskan tujuan, prosedur, dan lamanya tindakan pada klien/keluarga</li>
                </ol>

                <b>Tahapan Kerja</b>
                <ol start="7">
                    <li>Berikan kesempatan klien bertanya sebelum kegiatan dilakukan</li>
                    <li>Menanyakan keluhan utama klien / perasaan yang dirasakan saat ini</li>
                    <li>Atur posisi klien sebelum dilakukan terapi melukis. Berikan ruang agar tidak
                        mengganggu klien yang lainnya</li>
                    <li>Menetapkan ketertarikan klien terhadap melukis</li>
                    <li>Bagikan alat lukis yang diperlukan dan damping klien saat melukis apabila
                        klien bersedia di damping</li>
                    <li>Identifikasi pilihan/jenis lukisan.</li>
                    <li>Anjurkan klien untuk melukis sesuai dengan keinginan klien.</li>
                    <li>Berdiskusi dengan klien dengan tujuan berbagi pengalaman.</li>
                    <li>Bantu klien untuk memilih posisi yang nyaman.</li>
                    <li>Apabila sudah selesai berikan kesempatan klien untuk menjelaskan
                        lukisannya</li>
                    <li>Anjurkan klien untuk melukis kembali apabila klien ingin melukis</li>
                </ol>
                <b>Terminasi</b>
                <ol start="18">
                    <li>Evaluasi hasil kegiatan (kenyamanan klien)</li>
                    <li>Simpulkan hasil kegiatan</li>
                    <li>Berikan umpan balik positif</li>
                    <li>Kontrak pertemuan selanjutnya</li>
                    <li>Akhiri kegiatan dengan cara yang baik</li>
                    <li>Bereskan alat-alat</li>
                    <li>Cuci tangan</li>
                    <li>Catat hasil kegiatan di dalam catatan keperawatan</li>
                </ol>
            </td>
