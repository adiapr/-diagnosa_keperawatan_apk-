            <td>Persiapan Alat dan Bahan</td>
            <td>
                <ol>
                    <li>Menstrimulasi partisipasi yang aktif</li>

                    <li> Mendorong untuk mempelajari hal dan fungsi yang baru</li>
                    <li> Mendorong munculnya kesempatan untuk sukses, menjadi positif dan</li>
                    menyenangkan di dalam sosialisasi.</li>
                    <li>Meningkatkan motivasi</li>
                    <li> Pengembangan diri</li>
                    <li> Meningkatkan kemandirian dan arah diri.</li>
                    <li> Meningkatkan kesadaran diri dan</li>
                    <li> Memperkuat memori</li>
                    <li>Dapat meningkatkan konsep diri dapat terjadi karena tumbuhnya percaya diri
                        dalam bersosialisasi, sehingga memudahkan meraeka untuk memandang dirinya
                    lebih positif.</li>
                    <li> Mengeksplorasi perasaan klien</li>
                    <li> Mengembangkan keterampilan sosial</li>
                    <li> Mengurangi kecemasan</li>
                    <li> Mampu mengatasi tekanan fisik seperti nyeri.</li>
                </ol>
            </td>
